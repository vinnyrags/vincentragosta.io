<template>
  <div class="flex-column" :class="additionalClasses()" :style="cssVars">
    <div class="flex-column__bg" v-if="hasBg(this.$props)">
      <video v-if="video" class="flex-column__video" type="video/mp4" :src="video" autoplay muted loop></video>
      <img v-if="image" class="flex-column__image" :src="image"/>
    </div>
    <div class="flex-column__wrap">
      <slot></slot>
    </div>
  </div>
</template>

<script>
import sharedContainer from "@/assets/scripts/components/shared-container";
import alignmentProps from '@/assets/scripts/props/alignment';
import {hasBg} from "@/assets/scripts/functions/bg/hasBg";
import {cssVars} from "@/assets/scripts/functions/cssVars";

export default {
  // eslint-disable-next-line vue/multi-word-component-names
  name: 'FlexColumn',
  props: {
    width: {
      type: String,
      default: '12'
    },

    xs: [Boolean, String],
    sm: [Boolean, String],
    md: [Boolean, String],
    lg: [Boolean, String],
    xl: [Boolean, String],

    ...sharedContainer.props,
    ...alignmentProps,
  },
  components: {},
  methods: {
    hasBg,
    modifiers() {
      let modifiers = [];
      return [...modifiers, ...(sharedContainer.methods.modifiers(this.$props))].map((modifier) => {
        return 'flex-column--' + modifier;
      });
    },
    extraClasses() {
      let classes = [];

      if (this.width) {
        classes.push('flex-column-' + this.width);
      }

      const responsiveBreakpoints = this.responsiveBreakpoints();
      if (responsiveBreakpoints && responsiveBreakpoints.length) {
        responsiveBreakpoints.forEach((breakpoint) => {
          classes.push('flex-column-' + breakpoint.breakpoint + '-' + breakpoint.width);
        });
      }

      return classes;
    },
    additionalClasses() {
      return [...this.modifiers(), ...this.extraClasses()].join(' ');
    },
    responsiveBreakpoints() {
      let breakpoint = [];
      if (this.xs) {
        breakpoint.push({
          breakpoint: 'xs',
          width: typeof this.xs === 'string' ? this.xs : this.width
        })
      }
      if (this.sm) {
        breakpoint.push({
          breakpoint: 'sm',
          width: typeof this.sm === 'string' ? this.sm : this.width
        })
      }
      if (this.md) {
        breakpoint.push({
          breakpoint: 'md',
          width: typeof this.md === 'string' ? this.md : this.width
        })
      }
      if (this.lg) {
        breakpoint.push({
          breakpoint: 'lg',
          width: typeof this.lg === 'string' ? this.lg : this.width
        })
      }
      if (this.xl) {
        breakpoint.push({
          breakpoint: 'xl',
          width: typeof this.xl === 'string' ? this.xl : this.width
        })
      }
      return breakpoint;
    },
  },
  computed: {
    cssVars
  },
  mounted() {
  }
}
</script>