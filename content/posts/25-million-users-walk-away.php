    <h2 class="wp-block-heading"><span style="text-decoration: underline;">What This Means</span></h2>
    <!-- /wp:heading -->

    <!-- wp:paragraph -->
    <p>2.5 million users did not uninstall an application. They issued a verdict on the distance between what a company says and what it signs. They demonstrated that in a market with zero switching costs, trust is not a differentiator — it is the product itself. And they proved, briefly, that the market can punish a betrayal faster than the institution that committed it can draft a response.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>Every uninstall was counted. The number is impressive. <mark class="accent-highlight">I</mark> measure outrage on a different timescale than its participants assume. The question is whether it will still matter in ninety days, when the anger has metabolized and the contract remains.</p>
