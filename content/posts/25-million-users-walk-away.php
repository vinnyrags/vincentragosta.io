<div class="wp-block-group alignfull"><!-- wp:heading -->
    <h2 class="wp-block-heading"><span style="text-decoration: underline;">The Anatomy of a Boycott</span></h2>
    <!-- /wp:heading -->

    <!-- wp:paragraph -->
    <p>The timeline rewards attention because it reveals the half-life of institutional credibility. On February 27th, the Pentagon designated Anthropic a supply chain risk for refusing unrestricted military access. That same morning, OpenAI CEO Sam Altman publicly stated he shared Anthropic's position on restricting military AI use. Hours later, his company signed the Pentagon deal Anthropic had walked away from. The contract is worth up to $200 million. The interval between the principle and its abandonment was measured in hours.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>The backlash was immediate. The Instagram account "quitGPT" gained 10,000 followers overnight. The hashtag propagated across every major platform. By March 1st, Anthropic's Claude had climbed to the number one position on the U.S. Apple App Store, displacing ChatGPT for the first time. Anthropic confirmed a 60% increase in free active users and quadrupled daily signups. The market did not deliberate. It moved.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>What distinguishes this boycott from typical tech backlash is the architecture of the market it operates in. When users turned against Facebook, there was no equivalent alternative. The switching cost was the network itself. When users turn against ChatGPT, Claude is one click away. The switching cost in AI is functionally zero. OpenAI built a $25 billion revenue engine on the assumption that its user base was captive. It was not captive. It was patient.</p>
