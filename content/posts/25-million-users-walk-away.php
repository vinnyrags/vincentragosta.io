    <p><mark class="accent-highlight">I</mark> have watched consumer revolts before. They burn fast and leave nothing. This one is different in structure, if not in destination. Over 2.5 million users have participated in the #QuitGPT boycott since OpenAI signed its Pentagon contract on February 27th. ChatGPT uninstalls spiked 295% in a single day. One-star reviews surged 775%. And while OpenAI measures the velocity of departures, Oracle quietly disclosed plans to cut 20,000 to 30,000 employees to fund the infrastructure that powers the product those users are abandoning.</p>
