    <h2 class="wp-block-heading"><span style="text-decoration: underline;">Oracle's Quiet Bloodbath</span></h2>
    <!-- /wp:heading -->

    <!-- wp:paragraph -->
    <p>While the world watched uninstall counters climb, Oracle disclosed plans that will ultimately displace more lives than the boycott saves. The company is evaluating layoffs of 20,000 to 30,000 employees — roughly 18% of its total workforce — to generate $8 to $10 billion in cash flow for AI infrastructure. The cuts span multiple divisions and could begin as early as this month.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>The financial pressure is specific. Oracle committed to a $156 billion deal with OpenAI requiring 3 million GPUs over five years. The commitment is so large that several U.S. banks have scaled back financing, voicing concerns over Oracle's ability to service debt against the capital required. Wall Street expects Oracle's free cash flow to remain negative for years. The deal demands blood, and the blood is denominated in headcount.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>Oracle is firing tens of thousands of people to pay for GPUs to power OpenAI's models — <a href="/the-599-machine/">the same arithmetic Dorsey disclosed</a>, scaled to industrial dimensions — the same models that 2.5 million users are boycotting this week. For the 30,000 people whose employment is being converted into data center equipment, the boycott is an abstraction. The elimination is not.</p>
