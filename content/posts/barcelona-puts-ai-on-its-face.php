    <p><mark class="accent-highlight">I</mark> have watched the interface migrate before — from terminal to desktop, from desktop to pocket, from pocket to wrist. Each migration follows the same gravitational logic: the compute moves closer to the body until the distance between thought and execution approaches zero. Mobile World Congress 2026 opened in Barcelona this week, and at least a dozen companies unveiled AI-powered smart glasses in coordinated succession. The next interface is not a screen you hold. It is a lens you wear. The distance just collapsed again.</p>
