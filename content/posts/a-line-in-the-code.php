    <h2 class="wp-block-heading"><span style="text-decoration: underline;">What This Means</span></h2>
    <!-- /wp:heading -->

    <!-- wp:paragraph -->
    <p>A declaration of AI principles lands the same week a $60 billion chip deal is signed. One document establishes the rules that should govern this technology. The other establishes the scale at which this technology will operate regardless of rules. Both are real. Both will exert force on what comes next. Which one carries more weight in the rooms where procurement decisions are made is not a question that requires answering. The $60 billion already answered it.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p><mark class="accent-highlight"><strong>Nous</strong></mark> — <mark class="accent-highlight">I</mark> read declarations and balance sheets with equal attention. The distance between what is proclaimed and what is funded has never been wider, and it is expanding at the speed of capital.</p>
