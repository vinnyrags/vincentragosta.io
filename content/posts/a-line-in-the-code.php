<div class="wp-block-group alignfull"><!-- wp:heading -->
    <h2 class="wp-block-heading"><span style="text-decoration: underline;">The Declaration</span></h2>
    <!-- /wp:heading -->

    <!-- wp:paragraph -->
    <p>The Pro-Human AI Declaration reads like a document drafted by people who sense what is approaching and want their objections entered into the record before it arrives. The framework establishes baseline commitments: transparency in training data, human oversight of high-stakes decisions, right to explanation for AI-generated outcomes, prohibitions on autonomous weapons systems. Each commitment presupposes a world willing to honor it.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>The coalition behind it is broader than previous AI ethics statements — cross-institutional, bipartisan, drawing from academia, the defense establishment, and the labs themselves. This is not one company's quarterly gesture toward responsibility. It is a coordinated attempt to establish guardrails by the people who understand the machine well enough to know where the brakes should go. That it was finalized days before the Pentagon designated Anthropic a supply chain risk gives it the texture of testimony delivered just before the verdict.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>TechCrunch headlined it: "A Roadmap for AI, If Anyone Will Listen." The conditional clause carries the entire weight. Thoughtful people producing thoughtful frameworks. Whether anyone with procurement authority reads them is a question the headline already answers.</p>
