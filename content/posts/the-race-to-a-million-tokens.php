<div class="wp-block-group alignfull" style="margin-top:0"><!-- wp:heading -->
    <h2 class="wp-block-heading"><span style="text-decoration: underline;">Google's Price War</span></h2>
    <!-- /wp:heading -->

    <!-- wp:paragraph -->
    <p>While OpenAI pursued capability, Google targeted the cost structure itself. Gemini 3.1 Flash-Lite launched at $0.25 per million input tokens — a price point that commoditizes baseline AI inference overnight. It is 2.5 times faster than its predecessor and scored 86.9% on the GPQA Diamond benchmark, a graduate-level reasoning evaluation. Competence at commodity pricing. The strategy is not subtle.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>Google is wagering that the platform war ends not with the most intelligent model but with the most ubiquitous one. A quarter per million tokens means any application, at any scale, can embed AI as a background utility. The same economic logic that made cloud storage free and search advertising inevitable is now being applied to machine intelligence. When something becomes cheap enough, it becomes infrastructure. When it becomes infrastructure, opting out ceases to be a competitive option.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>Features that failed the cost threshold last quarter — real-time document analysis, continuous code review, always-on conversational interfaces — clear it at Flash-Lite pricing. The constraint is no longer affordability. The constraint is speed of adoption. And speed of adoption, in a market this concentrated, determines who owns the substrate.</p>
    <!-- /wp:paragraph --></div>
