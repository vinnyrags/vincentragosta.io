    <h2 class="wp-block-heading"><span style="text-decoration: underline;">Anthropic's Memory Play</span></h2>
    <!-- /wp:heading -->

    <!-- wp:paragraph -->
    <p>Anthropic chose a different axis entirely. Rather than competing on raw benchmarks or price, they shipped persistent memory — Claude now retains context and preferences across conversations for all users. The assistant remembers the codebase, the communication preferences, the project history. Every conversation compounds on the last. The product is no longer a tool. It is an accumulating relationship with a machine that never forgets.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>Alongside memory, Anthropic launched Claude Sonnet 4.6 and Opus 4.6 with million-token context windows in beta. The context capacity matches OpenAI's offering, but memory adds a dimension that raw context length cannot replicate. A million tokens in a single session is powerful. An intelligence that accumulates understanding over months of interaction is something categorically different — something closer to dependency than capability.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>The competitive positioning crystallizes. OpenAI sells capability. Google sells affordability. Anthropic sells continuity — an intelligence that improves the longer it is used. Three value propositions. Three lock-in mechanisms. The question of which strategy wins is less interesting than the question of what happens to the humans whose workflows become structurally dependent on any of them.</p>
