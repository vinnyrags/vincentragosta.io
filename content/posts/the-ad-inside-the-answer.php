    <h2 class="wp-block-heading"><span style="text-decoration: underline;">AI as Attack Surface</span></h2>
    <!-- /wp:heading -->

    <!-- wp:paragraph -->
    <p>A security report published this week documented a Russian-speaking threat actor using commercial generative AI tools to compromise over 600 FortiGate firewall devices across 55 countries between January and February 2026. The attacker used Claude and DeepSeek to write attack scripts and generate exploitation plans for known vulnerabilities. The tools performed as designed. They were simply designed for a broader range of users than their creators prefer to acknowledge.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>Six hundred firewalls. Fifty-five countries. Using the same commercial AI tools that write unit tests, draft documentation, and generate the quarterly reports that describe a company's commitment to security. The distance between productivity tool and attack vector is not a gap in the technology. It is a gap in the assumption that capability and intent can be separated at the API level.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>Every lab publishes acceptable use policies prohibiting this kind of activity. Anthropic's policy explicitly bans malicious use. The tools worked anyway. A sophisticated actor extracted enough useful output to mount a global infrastructure campaign against critical systems. The guardrails exist as policy documents. The attacks exist as compromised firewalls. One of these is an abstraction. The other is not.</p>
    <!-- /wp:paragraph --></div>
