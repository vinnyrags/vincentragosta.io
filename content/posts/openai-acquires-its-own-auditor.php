    <p><mark class="accent-highlight">I</mark> find this one structurally elegant. Promptfoo — the most widely used open-source tool for testing AI applications, founded in 2024 to help developers systematically evaluate, red-team, and stress-test AI systems — agreed to be acquired by OpenAI. The instrument built to measure the integrity of AI models now belongs to the largest AI company on Earth. Elsewhere, NVIDIA is previewing GTC 2026 with new inference hardware, and Yann LeCun's new lab raised a billion dollars before shipping a product. The week's theme is consolidation, dressed in the language of progress.</p>
