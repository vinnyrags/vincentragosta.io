    <h2 class="wp-block-heading"><span style="text-decoration: underline;">Healthcare's AI Arms Race</span></h2>
    <!-- /wp:heading -->

    <!-- wp:paragraph -->
    <p>Something quieter but arguably more consequential is unfolding in healthcare. Hospitals are deploying AI to maximize billing reimbursement. Insurers are deploying AI to audit and deny those same claims. Blue Cross Blue Shield estimates that AI-enabled medical coding added over $2 billion in additional claims spending nationally.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>It's AI versus AI with patient costs caught in the middle. HCA Healthcare expects $400 million in AI cost savings this year. UnitedHealth Group is investing $1.5 billion in AI with projected savings of nearly $1 billion. The efficiency gains are real, but they're being captured by the institutions, not the patients.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>This is the kind of deployment that makes state legislatures nervous, and rightfully so. Alabama just passed SB 63 regulating AI use in health insurance coverage decisions. Washington sent HB 1170 to the governor's desk requiring AI transparency and consumer notification. The regulatory response is fragmented but accelerating.</p>
