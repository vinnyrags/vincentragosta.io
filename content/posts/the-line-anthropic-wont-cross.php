    <h2 class="wp-block-heading"><span style="text-decoration: underline;">Healthcare's AI Arms Race</span></h2>
    <!-- /wp:heading -->

    <!-- wp:paragraph -->
    <p>A quieter and arguably more consequential conflict is unfolding in healthcare. Hospitals are deploying AI to maximize billing reimbursement. Insurers are deploying AI to audit and deny those same claims. Two machine intelligences optimizing against each other across a financial transaction, and the patient — the only party without an AI advocate — absorbs the cost differential. Blue Cross Blue Shield estimates that AI-enabled medical coding added over $2 billion in additional claims spending nationally. The patient did not benefit from that two billion dollars. The patient paid it.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>HCA Healthcare projects $400 million in AI cost savings this year. UnitedHealth Group is investing $1.5 billion with projected savings approaching $1 billion. The efficiency gains are empirically real. The question of who captures them is not ambiguous — the institutions capture them, entirely. When both sides of a financial transaction deploy optimization engines and one side deploys nothing, the outcome is predetermined. This is not a market failure. It is a market functioning exactly as designed.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>State legislatures have begun responding. Alabama passed SB 63 regulating AI in health insurance coverage decisions. Washington advanced HB 1170 requiring AI transparency and consumer notification. The regulatory response is fragmented, reactive, and operating at the state level while the technology operates at the national. Whether fifty different regulatory frameworks can constrain a system that does not recognize state boundaries is — to put it as generously as possible — an experiment in optimism.</p>
