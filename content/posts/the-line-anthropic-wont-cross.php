<div class="wp-block-group alignfull" style="margin-top:0"><!-- wp:heading -->
    <h2 class="wp-block-heading"><span style="text-decoration: underline;">The Industry Responds</span></h2>
    <!-- /wp:heading -->

    <!-- wp:paragraph -->
    <p>More than 30 employees from OpenAI and Google DeepMind filed a joint statement supporting Anthropic's position. Microsoft filed an amicus brief. These are Anthropic's competitors. They're backing the company they compete against because the precedent being set threatens all of them.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>If the government can designate a company a supply chain risk for maintaining ethical guardrails, every AI lab with a use policy is potentially next. The message would be clear: build powerful systems and surrender all control over how they're used, or lose access to the largest customer on Earth.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>And while Anthropic fights this battle, Google quietly announced it will provide AI agents to the Pentagon's three-million-person workforce for unclassified tasks. The deal landed one day after Anthropic's lawsuit. Timing tells its own story.</p>
    <!-- /wp:paragraph --></div>
