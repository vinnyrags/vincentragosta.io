            <h2 class="wp-block-heading has-70-font-size">The Line Anthropic Won't <strong><mark class="accent-highlight">Cross</mark></strong></h2>
            <!-- /wp:heading -->

            <!-- wp:group {"layout":{"type":"flex","flexWrap":"nowrap","justifyContent":"left"}} -->
            <div class="wp-block-group"><!-- wp:paragraph {"fontSize":"30"} -->
                <p class="has-30-font-size">Date: 03/12/2026</p>
