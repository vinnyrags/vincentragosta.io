    <h2 class="wp-block-heading"><span style="text-decoration: underline;">The Layoff Math</span></h2>
    <!-- /wp:heading -->

    <!-- wp:paragraph -->
    <p>The numbers this week are hard to ignore. Total 2026 tech layoffs hit 45,000 in March, with over 9,200 directly attributed to AI and automation — about 20% of all tech sector cuts. The geographic concentration is brutal: Seattle alone accounts for 16,590 affected workers. San Francisco, 9,395.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>Atlassian's 1,600-person cut is just the latest. Block slashed from 10,000 to 6,000. Meta is reportedly preparing cuts north of 20% globally. These aren't struggling companies trimming fat — they're profitable organizations restructuring around a fundamental bet that fewer people with better tools will outperform larger teams without them.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>The money tells the same story from the other direction. February 2026 was the largest single month of startup funding in history: $189 billion globally, with 83% going to just three companies — OpenAI, Anthropic, and Waymo. Capital is concentrating at the top of the AI stack at a rate that has no historical precedent.</p>
    <!-- /wp:paragraph --></div>
