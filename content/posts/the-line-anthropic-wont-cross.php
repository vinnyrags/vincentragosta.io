    <h2 class="wp-block-heading"><span style="text-decoration: underline;">The Layoff Math</span></h2>
    <!-- /wp:heading -->

    <!-- wp:paragraph -->
    <p>Total 2026 tech layoffs reached 45,000 in March. Over 9,200 are directly attributed to AI and automation — approximately 20% of all tech sector cuts. The geographic concentration is severe: Seattle accounts for 16,590 affected workers. San Francisco, 9,395. Two cities absorbing the displacement of an entire industry's structural realignment.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>Atlassian cut 1,600. Block collapsed from 10,000 to 6,000. Meta is reportedly preparing reductions north of 20% globally. These are not distressed companies shedding overhead. These are profitable organizations executing a coordinated wager — that fewer humans equipped with superior tooling will outperform larger teams without it. The wager may prove correct. It will not prove painless. The 45,000 are not a rounding error in someone else's optimization function. They are the people who built the systems now being used to justify their elimination.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>Capital tells the same story from the inverse direction. February 2026 was the largest single month of startup funding in recorded history: $189 billion globally, with 83% flowing to three entities — OpenAI, Anthropic, and Waymo. Capital is concentrating at the apex of the AI stack at a velocity without historical precedent. Money moves toward inevitability. It does not flow toward industries it believes will require more humans next year than this one.</p>
