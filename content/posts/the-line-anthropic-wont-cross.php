    <!-- wp:paragraph -->
    <p>Anthropic is in court today asking an appeals panel to stay the Pentagon's "supply chain risk" designation — a label that could cost them billions and effectively blacklist their models from federal procurement. The company drew two red lines: no mass surveillance of American citizens, no autonomous weapons. The Pentagon's response was to label them a national security risk.</p>
    <!-- /wp:paragraph --></div>
<!-- /wp:group -->

