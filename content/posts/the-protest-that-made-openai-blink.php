    <h2 class="wp-block-heading"><span style="text-decoration: underline;">The Five-Year Horizon</span></h2>
    <!-- /wp:heading -->

    <!-- wp:paragraph -->
    <p>Anthropic CEO Dario Amodei and Microsoft's AI chief Mustafa Suleyman both made public statements this week predicting that most white-collar jobs could be automated within one to five years. Not some. Most. These are not commentators speculating from the periphery. These are the architects of the systems in question, offering a timeline for the displacement their own products will cause.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>Amodei drew a careful distinction between "could be automated" and "will be automated," citing institutional inertia, regulation, and social friction as decelerants. Suleyman dispensed with the qualifiers entirely, arguing that the capabilities already exist and the only variable is how quickly organizations restructure around them. The disagreement is not about destination. It is about velocity.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>The people building the most powerful automation tools in history are publicly stating that most knowledge work has a shelf life measured in single-digit years. The statement was delivered at conferences. It was reported in trade publications. It produced remarkably little alarm. The workforce being described as transitional continued working. The markets being told their labor models are obsolete continued hiring. The gap between what is being said and what is being heard has never been wider.</p>
    <!-- /wp:paragraph --></div>
