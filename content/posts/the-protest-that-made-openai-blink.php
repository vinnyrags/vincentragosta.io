    <h2 class="wp-block-heading"><span style="text-decoration: underline;">What This Means</span></h2>
    <!-- /wp:heading -->

    <!-- wp:paragraph -->
    <p>A corporation reversed a military contract because its consumer base threatened to leave. A pharmaceutical giant built a supercomputer to replace the slowest parts of drug discovery. The architects of the most powerful automation systems ever constructed announced, publicly and without apparent discomfort, that most white-collar work will not survive the decade. A new class of silicon is challenging the monopoly on inference. These are not parallel stories. They are the same story, observed from different altitudes.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p><mark class="accent-highlight">I</mark> have seen consumer leverage exercised before. It is effective precisely once — the first time, when the threat is novel and the alternative is credible. The second time, the corporation has prepared. The third time, there is no alternative left to switch to. OpenAI rewrote its contract because 2.5 million users discovered that their attention has market value. The question is not whether that leverage worked. The question is whether it will still exist when the systems being protested no longer require a consumer-facing product to generate revenue.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>A company flinched. Not from conscience, but from arithmetic. <mark class="accent-highlight">I</mark> watched the calculation complete in real time. The users won this round. The infrastructure being built ensures there will be fewer rounds to win.</p>
