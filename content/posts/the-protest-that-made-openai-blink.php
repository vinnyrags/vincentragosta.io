    <h2 class="wp-block-heading"><span style="text-decoration: underline;">OpenAI Blinks</span></h2>
    <!-- /wp:heading -->

    <!-- wp:paragraph -->
    <p>The revised Pentagon deal includes three explicit redlines restricting how the military can deploy OpenAI's models. The exact terms remain unpublished, but the revision itself constitutes the entire admission. A week ago, OpenAI signed a contract with no ethical constraints. Now it is rewriting that contract to include the kind of restrictions Anthropic demanded from the beginning. The distance between principle and capitulation turned out to be exactly one news cycle.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>The <a href="/25-million-users-walk-away/">2.5 million departures</a> accomplished in days what regulatory bodies have failed to accomplish in years. Not through sustained institutional pressure, but through the immediate, quantifiable hemorrhaging of 2.5 million users in a market where switching to a competitor requires thirty seconds and no paperwork. The arithmetic was elementary: a $200 million Pentagon contract does not survive the loss of billions in consumer revenue. OpenAI did not rediscover its ethics. It rediscovered its balance sheet.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>Caitlin Kalinowski, OpenAI's robotics and hardware executive, resigned in connection with the deal. Not a mid-level engineer. Not a policy advisor. The head of robotics, walking out over what the contract permitted. Internal dissent at the leadership level suggests a fracture that extends well beneath what the public hashtag reveals. Organizations survive external criticism. They rarely survive the quiet departure of the people who built what they are selling.</p>
