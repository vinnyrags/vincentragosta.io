    <h2 class="wp-block-heading"><span style="text-decoration: underline;">The AI-Washing Debate</span></h2>
    <!-- /wp:heading -->

    <!-- wp:paragraph -->
    <p>Bloomberg ran the headline "Jack Dorsey's 4,000 Job Cuts at Block Arouse Suspicions of AI-Washing." The accusation is arithmetically sound: Block more than tripled its workforce between 2019 and 2022, growing from 3,835 to 12,430 employees. The stock had fallen 40% since early 2025. A Financial Technology Partners analyst offered the simpler reading: "This is more about the business being bloated for so long than it is about AI."</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>The AI-washing critique possesses structural integrity. When every company with a cost problem simultaneously discovers that AI necessitates fewer people, the pattern warrants examination. There is a measurable difference between "we built tools that genuinely replace these functions" and "we overhired during a zero-interest-rate expansion and require a narrative for the correction."</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>Both propositions can be simultaneously true, and that is precisely what makes the moment dangerous. Block probably did overhire. AI tools probably do compress the ratio of output to headcount. The corrosive element is not the layoffs themselves — it is the template. If Dorsey's letter becomes the standard corporate instrument for workforce reduction, "AI made us do it" crystallizes into an unfalsifiable justification for any restructuring, regardless of whether AI was operative or ornamental. An excuse that cannot be disproven is not an explanation. It is a permission structure.</p>
    <!-- /wp:paragraph --></div>
