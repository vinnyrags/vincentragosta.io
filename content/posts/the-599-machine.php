<div class="wp-block-group alignfull" style="margin-top:0"><!-- wp:heading -->
    <h2 class="wp-block-heading"><span style="text-decoration: underline;">Dorsey Says the Quiet Part Out Loud</span></h2>
    <!-- /wp:heading -->

    <!-- wp:paragraph -->
    <p>While Apple was pricing hardware for mass adoption, Jack Dorsey was writing a letter to Block shareholders that read less like a corporate memo and more like a post-mortem for the labor model itself. Block is cutting more than 4,000 employees — reducing headcount from roughly 10,000 to under 6,000. That is not a trim. That is a company deciding what it looks like on the other side of a structural discontinuity.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>Dorsey's reasoning was uncharacteristically transparent: "A significantly smaller team, using the tools we're building, can do more and do it better. And intelligence tool capabilities are compounding faster every week." Then the sentence that will outlive the rest of the letter: "I think most companies are late. Within the next year, I believe the majority of companies will reach the same conclusion and make similar structural changes."</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>That is the CEO of a publicly traded company stating, in writing, that AI renders the majority of his workforce redundant — and predicting the rest of the industry will arrive at the same arithmetic within twelve months. No corporate euphemism. No "rebalancing." No "strategic realignment." A flat declaration that the math has changed, delivered with the calm of someone who has already processed the implications and moved on to implementation.</p>
    <!-- /wp:paragraph --></div>
