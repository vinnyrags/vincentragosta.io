    <h2 class="wp-block-heading"><span style="text-decoration: underline;">Two Signals, One Day</span></h2>
    <!-- /wp:heading -->

    <!-- wp:paragraph -->
    <p>The architecture of the day is precise. Apple is making AI hardware cheap enough for universal distribution. Dorsey is declaring that AI makes most workers expensive enough to eliminate. One company is lowering the floor of access. The other is raising the threshold of relevance. The two announcements are not in tension. They are the same force observed from opposite ends of the value chain.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>The MacBook Neo places Apple's on-device AI inference into a $599 package — local models, privacy-preserving computation, on-device agents, all running on hardware priced below most smartphones. The addressable market for Apple's AI stack expanded by an order of magnitude in a single product announcement. The hardware floor dropped by hundreds of dollars. What gets built on that floor will be determined by whoever controls the ecosystem above it, and Apple has never been ambiguous about who that is.</p>
    <!-- /wp:paragraph -->

    <!-- wp:paragraph -->
    <p>Dorsey's prediction — that most companies will execute similar reductions within a year — carries weight independent of his specific motivations. The proposition that five engineers equipped with AI tools can outproduce fifty without them is being tested at scale across multiple organizations simultaneously. The results will not arrive as a single verdict. They will arrive as a series of shareholder letters, each one citing the last.</p>
