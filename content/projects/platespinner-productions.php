<h2 class="wp-block-heading"><span style="text-decoration: underline;">Implementation</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A portfolio-driven redesign built for a company that keeps adding credits.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The redesign was built on WordPress with a content architecture centered on the production portfolio — each show and film project structured as its own entry with production details, press coverage, and media assets organized through custom fields rather than freeform page content. The team bios section supported the company's intrapreneurial structure, giving each producer their own profile with credits, background, and industry affiliations. The news and press layer aggregated coverage from outlets like The New York Times, Variety, Playbill, and The Washington Post, with the content management system designed so the Plate Spinner team could add new press hits, update production credits, and announce new projects without developer involvement. The frontend prioritized clean typography, generous whitespace, and responsive image handling — letting the production imagery and press quotes carry the visual weight rather than competing with decorative design elements.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The site's structure anticipated growth — as Plate Spinner added new productions, team members, and press coverage, the content system scaled without requiring structural changes to the templates.</p>
