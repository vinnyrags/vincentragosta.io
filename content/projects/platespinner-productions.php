<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A production company building a major Broadway portfolio needed a digital presence that matched its ambition.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Plate Spinner Productions — founded by award-winning producer Diana DiMenna in 2018 — was rapidly building one of the more impressive producing portfolios on Broadway. By 2020, DiMenna's credits already included Tony-nominated productions like What the Constitution Means to Me (also a Pulitzer Prize finalist), Three Tall Women, Natasha, Pierre &amp; the Great Comet of 1812, and A Doll's House Part Two, alongside critically acclaimed work like Network, Girl From the North Country, and the documentary Restless Creature: Wendy Whelan. The company was also expanding its team, bringing on producers Brian Moreland and Rachel Sussman, and the existing website no longer reflected the scale or seriousness of the operation. Plate Spinner needed a redesign that could properly showcase a growing body of work across theatre, film, and digital media — and serve as the company's primary professional presence for industry partners, press, and collaborators.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The company's mission — creating dynamic stories that challenge artists and audiences to question the status quo — needed a digital platform that communicated that intent clearly and professionally.</p>
