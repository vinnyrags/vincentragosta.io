<!-- wp:paragraph -->
<p>The redesigned site launched in 2020 and has served as Plate Spinner Productions' digital home through a sustained run of high-profile work. Since launch, the company has produced or co-produced Broadway productions including Here Lies Love (the immersive David Byrne musical at the Broadway Theatre), Sweeney Todd, How to Dance in Ohio, The Sign in Sidney Brustein's Window, and Mike Birbiglia: The Old Man &amp; The Pool — alongside A Wrinkle in Time at Arena Stage and continued film development including What the Constitution Means to Me on Amazon Prime. The content architecture has handled every addition without structural modification — new productions, expanded team profiles, and press coverage from every major theatre publication flow through the same CMS the team has managed independently since handoff.</p>
