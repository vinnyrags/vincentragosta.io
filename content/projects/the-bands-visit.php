<h2 class="wp-block-heading"><span style="text-decoration: underline;">Implementation</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>Drawing on the ticketing infrastructure I had built for <a href="https://vincentragosta.io/projects/dear-evan-hansen/">Dear Evan Hansen</a>, I shaped a platform around a very different kind of production.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The custom WordPress theme drew on the ticketing infrastructure I had built for Dear Evan Hansen earlier that year, but the design and content strategy were entirely different. Where Dear Evan Hansen demanded bold color and high energy, The Band's Visit called for warmth and simplicity — earth tones, generous whitespace, and a layout that let the production's photography and storytelling do the work. The ticketing calendar anchored the site as the primary conversion tool, presenting the weekly schedule at the Ethel Barrymore with availability states and direct purchase routing. Cast and creative sections carried structured profiles for Shalhoub, Lenk, Stachel, John Cariani, and the full ensemble, alongside the creative team led by Cromer, choreographer Patrick McCollum, and orchestrator Jamshied Sharifi. When the national tour was announced, I extended the platform with city-by-city scheduling, venue details, and a navigation layer that gave the tour its own section without disrupting the Broadway content. The site also handled media galleries, press aggregation, a digital lottery integration, and merchandise through the show's retail partners.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">Reusing proven architecture from Dear Evan Hansen meant I could focus development time on what made this production unique rather than rebuilding ticketing infrastructure from scratch.</p>
