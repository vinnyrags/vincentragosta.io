<h2 class="wp-block-heading"><span style="text-decoration: underline;">Results</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>10 Tony Awards, 589 performances, and a platform that carried the production from Broadway to a national tour.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The Band's Visit won 10 Tony Awards at the 72nd ceremony in June 2018 — including Best Musical, Best Original Score, Best Book, Best Direction, and both leading actor awards — making it one of only four musicals in Broadway history to win the "Big Six." The platform supported the production through 589 performances and 36 previews at the Ethel Barrymore Theatre, breaking the venue's all-time box office record twice before the Broadway run closed on April 7, 2019. The tour infrastructure carried the show into its first national tour, launching at the Providence Performing Arts Center in June 2019 and playing over 25 cities — including the Kennedy Center, Cadillac Palace in Chicago, and the Golden Gate in San Francisco — before the touring industry suspended in March 2020. The tour resumed in October 2021 and continued through 2022.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">Building for a production this critically dominant sharpened the patterns I was developing across simultaneous Broadway projects like <a href="https://vincentragosta.io/projects/pretty-woman/">Pretty Woman</a> and <a href="https://vincentragosta.io/projects/the-prom/">The Prom</a> — each one refining the approach for the next.</p>
