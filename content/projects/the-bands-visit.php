<h2 class="wp-block-heading"><span style="text-decoration: underline;">Background</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>The show everyone was talking about had almost no spectacle — the website had to channel that same restraint.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The Band's Visit — adapted from Eran Kolirin's 2007 Israeli film about an Egyptian police orchestra stranded in a remote desert town — arrived on Broadway at the Ethel Barrymore Theatre on November 9, 2017, after a sold-out world premiere at the Atlantic Theater Company Off-Broadway. Directed by David Cromer with music and lyrics by David Yazbek and a book by Itamar Moses, the production starred Tony Shalhoub as Tewfiq, Katrina Lenk as Dina, and Ari'el Stachel as Haled. By the time I started building the site, critical momentum was already extraordinary heading into the 2018 Tony season. Unlike most Broadway marketing sites that lean into spectacle, this one had to convert attention into ticket sales while reflecting a production whose entire identity was built on understatement.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">I built this platform at Situation Interactive during the show's first Broadway year, watching it go from critical darling to historic Tony sweep in real time.</p>
