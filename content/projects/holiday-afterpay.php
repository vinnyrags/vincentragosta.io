<h2 class="wp-block-heading"><span style="text-decoration: underline;">Results</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>Live on time, pixel-perfect, and driving product discovery through the peak holiday window.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The microsite launched on schedule at holiday.afterpay.com and served as a primary discovery channel for Afterpay's holiday campaign, routing shoppers directly to retail partner storefronts during the highest-volume shopping weeks of the year. The build shipped on a compressed timeline with no scope cuts — every window, every product, every snow particle made it to production exactly as designed. The experience ran through the full 2021 holiday season, supporting Afterpay's broader campaign that included their "Six Weeks of Gifting Better" sweepstakes and "Done Day" initiative. The microsite has since been retired — seasonal campaigns don't live forever — but the build remains one of the most technically ambitious frontend projects in my career and a proof point that precision CSS and vanilla JavaScript can deliver the kind of interactive experience that most teams would reach for a framework to build.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">This project proved that fast timelines and ambitious design aren't mutually exclusive — the constraint was the deadline, not the craft.</p>
