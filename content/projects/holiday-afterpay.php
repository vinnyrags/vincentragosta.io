<h2 class="wp-block-heading"><span style="text-decoration: underline;">Background</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>The biggest buy-now-pay-later brand in the world needed a holiday microsite — and it needed it fast.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Afterpay — the Australian-founded buy-now-pay-later platform acquired by Block, Inc. (formerly Square) in a $29 billion deal — was heading into the 2021 holiday season as the dominant BNPL brand in North America, with over 20 million customers and nearly 100,000 retail partners. The holiday season was existential for BNPL: consumer spending through Afterpay was up 34% year-over-year, in-store orders had surged 442% on Black Friday alone, and the platform had saved U.S. customers an estimated $459 million in credit card fees that year. Afterpay's marketing team wanted a dedicated holiday microsite at holiday.afterpay.com — not a standard landing page, but a fully interactive experience that would drive product discovery and funnel shoppers into partner retailers during the most competitive shopping window of the year. The timeline was fixed to the holiday calendar with zero flexibility on the launch date.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The brief was clear: build something visually ambitious on a holiday deadline — no extensions, no phased rollout, live or nothing.</p>
