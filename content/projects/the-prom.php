<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>Six Tony nominations, a Drama Desk Award, 309 performances, a national tour, and a Netflix film starring Meryl Streep.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The site served as the production's primary digital platform through 309 performances at the Longacre Theatre, closing August 11, 2019. The production grossed over $25 million at an average capacity of 85%. The tour system scaled to support the national tour launching in November 2021 at Playhouse Square in Cleveland, playing over 20 cities through October 2022. When Ryan Murphy adapted the musical into a Netflix film released December 11, 2020 — starring Meryl Streep, James Corden, Nicole Kidman, Kerry Washington, and Keegan-Michael Key — the site transitioned into a bridge between the stage production and its second life on screen, without requiring a rebuild.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">A site designed for an opening night at the Longacre scaled into a tour platform and a film marketing hub — the architecture absorbed each new phase without breaking.</p>
