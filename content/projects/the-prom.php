<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A show that made history at the Macy's Thanksgiving Day Parade needed a website with the same fearless energy.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The Prom opened at the Longacre Theatre on November 15, 2018 — the fourth Broadway production I was building simultaneously that year alongside <a href="https://vincentragosta.io/projects/dear-evan-hansen/">Dear Evan Hansen</a>, <a href="https://vincentragosta.io/projects/the-bands-visit/">The Band's Visit</a>, and <a href="https://vincentragosta.io/projects/pretty-woman/">Pretty Woman</a>. With book by Bob Martin and Chad Beguelin, music by Matthew Sklar, and direction and choreography by Casey Nicholaw, the show told the story of four fading Broadway stars descending on a small Indiana town to champion a lesbian student banned from bringing her girlfriend to prom. The principal cast included Brooks Ashmanskas, Beth Leavel, Caitlin Kinnunen, Isabelle McCalla, Christopher Sieber, and Michael Potts. A week after opening, Kinnunen and McCalla shared the first LGBTQ kiss in the history of the Macy's Thanksgiving Day Parade during a live performance of "It's Time to Dance." The show earned six Tony nominations including Best Musical and won the Drama Desk Award for Outstanding Musical. The message and the marketing were inseparable — the platform had to carry the same spirit of joy that defined the production.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">This was part of a 2018 cluster of Broadway builds that sharpened every part of my process — each production demanding a distinct visual identity while sharing underlying architectural patterns.</p>
