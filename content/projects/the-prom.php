<h2 class="wp-block-heading"><span style="text-decoration: underline;">Implementation</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>Animated backgrounds, a demanding color palette, and the trickiest design translation in my Broadway portfolio.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The custom WordPress theme launched ahead of the show's first preview and evolved through the Broadway run, tour announcement, and Netflix adaptation. The Prom's visual identity leaned heavily on saturated purples, pinks, and aquas — a celebratory aesthetic that demanded precision in CSS to land across breakpoints without feeling overwrought. Interactive backgrounds used CSS3 animations and JavaScript-driven parallax to create depth and movement, reinforcing the show's exuberance without dragging page performance. The ticketing calendar handled the Longacre's weekly schedule with availability states and direct purchase routing. When the national tour was announced, I extended the platform with city-by-city scheduling, venue details, and tour-specific ticketing endpoints — all editable by the production team through the CMS. Cast and creative sections carried structured profiles for the full principal company (Ashmanskas, Leavel, Kinnunen, McCalla, Sieber, Potts, Angie Schworer, Josh Lamon) and the creative team from Nicholaw through Pask, Pachtman, Roth, and Katz. The site also served as the central hub for press materials, media assets, and social content as the show's cultural footprint grew.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">Every keyframe in the background animations had to feel intentional — celebratory without being distracting, colorful without being garish. That balance was the hardest part of the build.</p>
