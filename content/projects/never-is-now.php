<h2 class="wp-block-heading"><span style="text-decoration: underline;">Implementation</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>Multi-track agenda infrastructure with integrated registration and hybrid delivery.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>I led development on a custom WordPress theme built to manage the full complexity of a conference operating at this scale. The agenda system was the centerpiece — a structured content architecture supporting 25+ sessions across six named tracks (Advocacy, Arts/Media/Technology, Education, Litigation, Safety/Security, and Student Leadership), with day-based filtering, session-type categorization, livestream availability indicators, and CEU credit designations for educators. Each session linked to its speakers, and each speaker profile resolved back to its sessions, creating a navigable two-way relationship across 100+ participants. Registration flowed through a Cvent integration supporting multiple ticket tiers — general admission, platinum passes with preferred seating, senior pricing, and free virtual access — with the WordPress frontend handling presentation while Cvent managed transaction processing and attendee data.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The frontend delivered a design system anchored by CSS custom properties — a bold green-and-blue palette with fluid responsive typography and generous spacing scaled across breakpoints. Google Tag Manager provided event tracking across registration funnels and session engagement. ASL interpretation support, wheelchair accessibility information, and kosher meal logistics were surfaced through the site's FAQ and registration flows, reflecting the ADL's commitment to inclusive access at every level of the experience.</p>
