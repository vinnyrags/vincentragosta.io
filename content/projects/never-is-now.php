<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>The world's largest summit on antisemitism needed a digital platform to match its scale.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Never Is Now is the Anti-Defamation League's annual summit — the largest gathering in the world dedicated to confronting antisemitism and hate. The event draws thousands of in-person attendees to New York City alongside a global virtual audience, featuring keynotes from heads of state, CEOs, educators, and civil rights leaders. The 2023 edition carried particular urgency: antisemitic incidents in the U.S. had surged to record levels, and the summit's programming expanded to include dedicated tracks for college and high school students for the first time. The ADL needed a digital platform that could serve as both the operational backbone of a multi-day, multi-track conference and a year-round content hub for the organization's advocacy work.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The site had to handle complex event logistics — registration tiers, agenda filtering, speaker discovery, accessibility compliance, and livestream integration — while maintaining the visual authority of the ADL's brand.</p>
