<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A world-touring illusionist needed a ticket calendar that could work within — not replace — an existing website.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Rob Lake — named "The World's Greatest Illusionist" by NBC, the youngest recipient of the Merlin Award for International Stage Magician of the Year, and an America's Got Talent quarter-finalist — performs to sold-out audiences across more than 60 countries. His 2025 touring schedule included international runs in Dubai and Mumbai, a Broadway engagement at the Broadhurst Theatre with The Muppets, and a national tour of performing arts centers across the United States. The primary website at roblake.com was built on Wix, and the team managing it had no interest in migrating away from it. But Wix had no native solution for the kind of ticket calendar the production needed — a dynamic, venue-aware system that could display dozens of tour dates across multiple regions and route each one to the correct ticketing provider. The constraint was clear: build a ticket calendar that could be embedded into the existing Wix site without requiring a platform migration or disrupting the workflows already in place.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The calendar needed to support a touring schedule that spans performing arts centers, casinos, international venues, and Broadway — each with its own ticketing partner and purchase flow.</p>
