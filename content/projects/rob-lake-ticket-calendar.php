<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A standalone WordPress calendar on its own subdomain, designed to embed seamlessly into a Wix parent site.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>I built the ticket calendar as a standalone WordPress application deployed to its own subdomain — calendar.roblake.com — purpose-built for embedding into the existing Wix site via iframe. The WordPress backend powers a custom event management system where each tour date is a structured content object with venue name, city, date, time, ticketing provider URL, and availability status. The frontend renders these dates through FullCalendar, presenting multiple view modes — month, week, list, and a touch-friendly scroll view powered by Mobiscroll for mobile audiences. Each calendar entry links directly to the appropriate ticketing provider for that venue, whether that's Ticketmaster for a performing arts center, a casino box office, or an international presenter's own ticketing platform. The design system was built to visually integrate with the Wix parent site — matching Rob Lake's brand palette, typography, and spacing so the embedded calendar reads as a native section of the page rather than a foreign widget. A custom REST API layer supports dynamic data retrieval with prefetch optimization, keeping the calendar responsive even as the tour schedule scales across dozens of dates and venues.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The WordPress admin gives the production team full control over the tour calendar — adding dates, updating venue links, marking shows as sold out, and managing regional groupings — all without touching the Wix site or involving a developer.</p>
