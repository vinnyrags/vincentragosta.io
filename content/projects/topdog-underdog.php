<h2 class="wp-block-heading"><span style="text-decoration: underline;">Background</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A Pulitzer Prize-winning play was returning to Broadway for the first time in twenty years — with two of the biggest names in film leading the cast.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Suzan-Lori Parks' Topdog/Underdog — the darkly comic two-hander about brothers named Lincoln and Booth navigating poverty, identity, and the street con game three-card monte — won the Pulitzer Prize for Drama in 2002, making Parks the first African American woman to receive the award. The original Broadway production at the Ambassador Theatre, directed by George C. Wolfe and starring Jeffrey Wright and Mos Def, ran for 144 performances. Twenty years later, producers David Stone, LaChanze, Rashad V. Chambers, Marc Platt, Debra Martin Chase, and The Shubert Organization mounted the play's first-ever Broadway revival at the John Golden Theatre, directed by four-time Tony winner Kenny Leon. The casting was the headline: Emmy Award winner Yahya Abdul-Mateen II making his Broadway debut as Booth opposite Tony-nominated Corey Hawkins as Lincoln. A production carrying that level of cultural weight — a landmark American play, a marquee director, and two film stars drawing audiences who might never have seen the original — needed a digital platform that could convert mainstream attention into ticket sales and serve as the definitive online presence for the revival.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The site needed to be live ahead of the first preview on September 27, 2022, and sustain the production through its entire run at the Golden Theatre.</p>
