<h2 class="wp-block-heading"><span style="text-decoration: underline;">Implementation</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A custom WordPress build with a ticketing calendar, structured cast profiles, and a visual identity built on the production's warm, graphic design language.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>I led development on a custom WordPress theme that served as the production's complete digital platform. The ticketing calendar was the primary conversion driver — an interactive, date-based interface rendering the full performance schedule at the John Golden Theatre with matinee and evening slots, availability states, and direct purchase links routing audiences into the ticketing provider's checkout flow with the correct performance pre-selected. The cast and creative section featured structured biographical profiles for Abdul-Mateen II and Hawkins, along with the full creative team — Parks, Leon, scenic designer Arnulfo Maldonado, costume designer Dede Ayite, lighting designer Allen Lee Hughes, and sound designer Justin Ellington — each with detailed credits managed through a dedicated content architecture that the production team could update independently. The frontend translated the production's visual identity into code: a cream and deep purple color palette, warm typographic scale, and spacious layouts that reflected the intimacy of a two-person play at a 500-seat theatre. Gravity Forms powered newsletter signup and audience engagement, and social integrations connected Facebook, Twitter, Instagram, and Show-Score. An American Express partnership badge and structured Schema.org data rounded out the production requirements.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The cast and creative profiles were the content centerpiece — with two film stars driving ticket sales, their biographical sections needed to carry the same weight as the ticketing infrastructure.</p>
