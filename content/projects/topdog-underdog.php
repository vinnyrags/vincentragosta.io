<h2 class="wp-block-heading"><span style="text-decoration: underline;">Results</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A Tony Award for Best Revival, over $4.5 million at the box office, and a platform that served the production from first preview through closing night.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The site launched ahead of previews on September 27, 2022, and served as the production's primary digital platform through its entire run at the John Golden Theatre. Topdog/Underdog officially opened on October 20 and ran through January 15, 2023 — a sixteen-week engagement grossing $4,575,950 at an average ticket price of $71.45. The production won the Tony Award for Best Revival of a Play, and both Corey Hawkins and Yahya Abdul-Mateen II earned Tony nominations for Best Leading Actor in a Play. The ticketing calendar drove conversions throughout the run, the cast profiles supported press coverage around two high-profile leads making their Broadway debuts in a Pulitzer-winning play, and the structured content layer gave the production team full editorial independence from launch through closing.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">Between this production and <a href="https://vincentragosta.io/projects/for-colored-girls/">for colored girls</a> — both built and launched within the same window at Situation Interactive — the shared architectural patterns proved their durability across two distinct productions with different visual identities, audience profiles, and run lengths.</p>
