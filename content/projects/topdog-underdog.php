<h2 class="wp-block-heading"><span style="text-decoration: underline;">Implementation</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A custom WordPress build with a ticketing calendar, structured cast profiles, and a visual identity rooted in the play's raw intensity.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>I led development on a custom WordPress theme that served as the production's complete digital platform. The ticketing calendar was the primary conversion surface — an interactive, date-based interface rendering the full performance schedule at the John Golden Theatre with real-time availability states and direct purchase links routing audiences into the ticketing provider's checkout flow with the correct performance pre-selected. The cast and creative section featured structured biographical profiles for Corey Hawkins and Yahya Abdul-Mateen II alongside the full creative team: director Kenny Leon, scenic designer Arnulfo Maldonado, costume designer Dede Ayite, lighting designer Allen Lee Hughes, and sound designer Justin Ellington. Each profile was managed through a dedicated content architecture that the production team could update without developer involvement. The frontend translated the production's visual identity into code — a stark, stripped-down aesthetic that matched the play's claustrophobic single-room setting and the tension between its two characters. SASS-driven component architecture kept the styling modular and maintainable. JavaScript handled interactive elements including the calendar UI, responsive navigation, and performance-driven animations that reinforced the production's energy without competing with its minimalism.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">Building this concurrently with the for colored girls site meant shared component patterns and a unified development workflow — two productions, one engineering approach.</p>
