<h2 class="wp-block-heading"><span style="text-decoration: underline;">Background</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A Pulitzer Prize-winning play was returning to Broadway for its twentieth anniversary — with two of Hollywood's most in-demand actors leading the revival.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Suzan-Lori Parks' Topdog/Underdog — the darkly comic two-hander about brothers named Lincoln and Booth by their father as a cruel joke — won the Pulitzer Prize for Drama in 2002, making Parks the first African-American woman to receive the award. Twenty years later, the play returned to Broadway in a new production directed by Tony winner Kenny Leon, starring Corey Hawkins as Lincoln and Yahya Abdul-Mateen II as Booth. The strictly limited sixteen-week engagement at the John Golden Theatre carried significant cultural weight: two film and television stars bringing fresh energy to a canonical American play about race, identity, masculinity, and the hustle of survival. This was one of two plays I led development on that opened the same week in spring 2022 — the other being <a href="https://vincentragosta.io/projects/for-colored-girls/">for colored girls who have considered suicide / when the rainbow is enuf</a>, which opened at the Booth Theatre just days apart. Both projects ran in parallel at Situation Interactive, demanding tight coordination and shared architectural decisions across two distinct productions.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">Previews began September 27, 2022 at the John Golden Theatre. The site needed to be live and converting before the first audience walked in.</p>
