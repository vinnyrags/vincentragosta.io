<h2 class="wp-block-heading"><strong>A safety consulting firm needed a professional web presence — fast.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>AMA Safety is a workplace safety consulting company offering OSHA compliance services, safety training, and job site inspections across disciplines including fall protection, confined space entry, hazard communication, forklift safety, and trenching and excavation. The company had been operating on referrals and word-of-mouth but lacked any digital presence — no website, no way for prospective clients to vet the business online before picking up the phone. In an industry where credibility is everything, that gap was costing them opportunities.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The ask was simple: build a clean, professional site that communicated competence and made it easy for potential clients to understand the services offered and make contact.</p>
