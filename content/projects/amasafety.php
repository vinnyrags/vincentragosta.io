<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A lean WordPress build optimized for clarity and conversion.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>I built the site on WordPress with a lightweight theme configured for speed and simplicity. The information architecture was straightforward — a homepage establishing the brand, dedicated service pages covering each consulting discipline (inspections, fall protection, confined space, hazard communication, ergonomics, and more), and a prominent contact section with a structured inquiry form. Every page was built mobile-first with responsive layouts, clean typography, and fast load times. No bloated page builders, no unnecessary plugins — just semantic markup, custom CSS, and WordPress doing what it does best as a content management platform.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The site was designed so the client could update service descriptions, add testimonials, and manage contact form submissions independently — zero developer dependency after handoff.</p>
