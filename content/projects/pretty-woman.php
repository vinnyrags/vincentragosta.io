<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>From a Chicago tryout to a global franchise — this production's trajectory demanded an architecture none of my other Broadway builds had attempted.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Pretty Woman: The Musical — based on the iconic 1990 romantic comedy — arrived on stage with music and lyrics by Bryan Adams and Jim Vallance, a book by Garry Marshall and J.F. Lawton, and direction and choreography by Jerry Mitchell. The production featured scenic design by David Rockwell, costumes by Gregg Barnes, lighting by Kenneth Posner and Philip S. Rosenberg, and sound design by John Shivers. After a world premiere at the Oriental Theatre in Chicago in March 2018, the show transferred to the Nederlander Theatre on Broadway with Samantha Barks making her Broadway debut as Vivian Ward opposite Andy Karl as Edward Lewis, joined by Orfeh as Kit De Luca, Jason Danieley as Philip Stuckey, and Eric Anderson as Mr. Thompson. From the start, the production team signaled that this would expand internationally — so unlike <a href="https://vincentragosta.io/projects/dear-evan-hansen/">Dear Evan Hansen</a> or <a href="https://vincentragosta.io/projects/the-bands-visit/">The Band's Visit</a>, where I built single-site platforms and extended them later, Pretty Woman needed multi-production architecture from day one.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">I built this from scratch at Situation Interactive, knowing the platform would need to serve productions across multiple countries, languages, and ticketing systems.</p>
