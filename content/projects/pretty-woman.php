<h2 class="wp-block-heading"><span style="text-decoration: underline;">Implementation</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A WordPress multisite platform connecting Broadway and tour as separate but unified production sites.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>WordPress Multisite was the distinguishing architectural decision. The Broadway production and national tour ran as separate network sites sharing a common theme and plugin set, giving each production its own editorial space — distinct cast pages, venue information, and ticketing endpoints — while eliminating duplicated code. The Broadway site at prettywomanthemusical.com served as the primary hub; the tour operated on its own subdomain with city-by-city scheduling and local ticketing links. The ticketing calendar drove conversions on both sites — on Broadway, it displayed the full weekly schedule at the 1,232-seat Nederlander; on tour, it scaled across dozens of cities with their respective local ticketing partners. Cast and creative sections used custom post types with Advanced Custom Fields, so the production team could swap performers, update headshots, and manage bios as the company evolved. Gravity Forms handled newsletter signup and group sales inquiries across both network sites. The frontend translated the show's signature red-and-black visual identity into responsive layouts built in SASS with mobile-first breakpoints.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">Multisite was the right call — it kept Broadway and tour connected at the infrastructure level while giving each production the editorial independence to operate on its own schedule. None of my other Broadway builds needed this pattern, which is what made Pretty Woman architecturally distinct.</p>
