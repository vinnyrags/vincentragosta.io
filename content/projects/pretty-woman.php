<h2 class="wp-block-heading"><span style="text-decoration: underline;">Results</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>$51 million at the Broadway box office, 420 performances, three Audience Choice Awards, and a platform that scaled from Chicago to a global franchise.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The platform launched ahead of the Chicago tryout in March 2018 and carried the production through 27 previews and 420 performances at the Nederlander Theatre, grossing over $51.1 million at 86% average capacity. The show broke the Nederlander's house record twice in its opening weeks, peaking at $1,266,873 in a single week. Andy Karl won the Audience Choice Award for Favorite Leading Actor, and Orfeh took both Favorite Featured Actress and Favorite Diva Performance. The multisite architecture proved its value when the North American tour launched in October 2021, scaling to over 50 cities across the U.S. and Canada. Beyond North America, the production opened in Hamburg, London's West End (Savoy Theatre), Brazil, the Netherlands, and a UK and Ireland tour — becoming a genuine global theatrical franchise built on the infrastructure I designed for a single Broadway opening.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">Pretty Woman is the project that validated the multisite approach — a platform designed for two productions ended up serving a global franchise across years and continents.</p>
