<h2 class="wp-block-heading"><span style="text-decoration: underline;">Background</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A culturally significant revival with star power at every level — and no room for delay.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Purlie Victorious: A Non-Confederate Romp Through the Cotton Patch — Ossie Davis' 1961 comedy about a Black preacher's scheme to reclaim his inheritance from a plantation owner — was returning to Broadway for the first time in over 60 years. Kenny Leon directed, Leslie Odom Jr. starred in his first Broadway role since Hamilton, and the cast included Kara Young, Vanessa Bell Calloway, Billy Eugene Jones, Jay O. Sanders, and Heather Alicia Simms. The producer roster — Samuel L. Jackson, Phylicia Rashad, Kerry Washington, Alan Alda — amplified the stakes. Previews began September 7, 2023, at the Music Box Theatre. Like the American Buffalo and for colored girls revivals I had built earlier, this was a limited engagement where every day of ticket sales mattered and the site had to be launch-ready well before the first audience arrived.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The cultural weight of the production — and the visibility of its producers — meant the digital presence had to be polished and performant from the first public moment.</p>
