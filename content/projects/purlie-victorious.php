<h2 class="wp-block-heading"><span style="text-decoration: underline;">Results</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>Six Tony nominations, a four-week extension, and a platform that carried the production from opening night to PBS.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The site supported Purlie Victorious through 23 previews and 151 performances at the Music Box Theatre, from the September 7 first preview through the February 4, 2024, closing. The production extended its limited engagement by four weeks beyond the originally announced closing date, driven by strong box office performance and critical acclaim. Six 2024 Tony Award nominations followed: Best Revival of a Play, Best Leading Actor (Leslie Odom Jr.), Best Featured Actress (Kara Young, who won), Best Direction (Kenny Leon), Best Scenic Design (Derek McLane), and Best Costume Design (Emilio Sosa). The production was recorded live at the Music Box Theatre in January 2024 and aired on PBS Great Performances on May 24, extending the show's reach well beyond its theatrical run.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">From limited engagement to Tony nominations to a PBS broadcast — the platform carried the production through every phase, including ones that were not part of the original plan.</p>
