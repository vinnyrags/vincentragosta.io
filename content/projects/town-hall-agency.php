<h2 class="wp-block-heading"><span style="text-decoration: underline;">Implementation</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>Custom WordPress theme with a webinar interface, structured case studies, and service architecture spanning three verticals.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>I led development on a custom WordPress theme built to support an agency launching with three distinct service verticals from day one. The content architecture separated nonprofit, education, and mission-driven corporate work into dedicated service sections — each with its own case study library, client showcases, and messaging framework — while sharing a unified navigation and design system across the site. The enlightenED interface was the most technically involved piece of the build. The webinar series — produced in partnership with Google Education — required a structured content system for managing sessions with dates, topics, speaker information, video embeds, and downloadable PDF takeaway documents. Each session needed its own landing experience with on-demand video playback, resource downloads, and contextual calls-to-action driving registration for upcoming sessions or newsletter signup for future content. Gravity Forms powered the lead capture and registration workflows across enlightenED and the agency's contact pathways, with form routing logic that directed inquiries to the correct team based on service vertical and inquiry type. The case study system used structured content objects with client name, vertical, service tags, and rich media — surfaced through filterable views that let visitors browse Town Hall's work by sector or capability.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The content architecture gave Town Hall's team full editorial independence — publishing case studies, managing webinar sessions, updating service pages, and capturing leads all happen through the WordPress admin without developer involvement.</p>
