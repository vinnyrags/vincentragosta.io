<h2 class="wp-block-heading"><span style="text-decoration: underline;">Implementation</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>Custom product archive with dynamic filtering, live search, and category sorting.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>I built the site on WordPress with WooCommerce powering the product catalog and checkout infrastructure, wrapped in a custom theme tailored to the brand's visual identity. The centerpiece of the build was the product archive — a faceted filtering system that lets customers narrow results by category, search by keyword in real time, and sort across the full inventory without page reloads. The filter and search components update the product grid dynamically via JavaScript, keeping the browsing experience fast and fluid even across hundreds of SKUs. Category taxonomy pages serve as defined entry points into each product vertical, while the main archive aggregates everything with the filter controls always accessible.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The frontend was built mobile-first with responsive layouts, lazy-loaded product images with intrinsic sizing for layout stability, and a cart system that handles quantity management and checkout without friction.</p>
