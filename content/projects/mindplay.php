<h2 class="wp-block-heading"><span style="text-decoration: underline;">Results</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>Live for previews, extended twice, and the digital home for a show that sold out its entire New York run.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The site launched ahead of Mindplay's first preview on December 15, 2024, and served as the production's primary digital platform through its entire Off-Broadway engagement at the Greenwich House Theater. The show officially opened on January 13, 2025, and was extended twice — first through April 20, then through May 11 — continuing the pattern of sold-out runs that had defined the production at the Geffen Playhouse, Arena Stage, and The Huntington. The ticketing calendar drove conversions throughout the engagement, the press acclaim section reinforced the critical consensus as new reviews published, and the newsletter integration built the show's direct audience database through the New York run. The site at mindplaynyc.com now serves as the archival record of Mindplay's New York premiere — a production backed by the producers of Hamilton and Dear Evan Hansen that arrived Off-Broadway with three sold-out regional engagements behind it and extended twice more in New York.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">Mindplay is a project where the design constraints were the reward — a dark, atmospheric build for a show about memory and illusion, backed by some of the most successful producers in American theatre.</p>
