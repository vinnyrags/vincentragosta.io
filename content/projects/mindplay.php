<h2 class="wp-block-heading"><span style="text-decoration: underline;">Background</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A sold-out hit from the producers of Hamilton and Dear Evan Hansen was coming to New York — and it needed a digital platform as precise as the performance itself.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Mindplay — the interactive solo show from Drama Desk-nominated mentalist and theatermaker Vinny DePonto — had already sold out runs at the Geffen Playhouse in Los Angeles, Arena Stage in Washington, D.C., and The Huntington in Boston before arriving Off-Broadway at the Greenwich House Theater in December 2024. The show was co-written by DePonto and playwright Josh Koenigsberg, directed by Andrew Neisler, and produced by Tony, Emmy, and Olivier-winning producer Eva Price (Dear Evan Hansen) and Tony Award-winning director Thomas Kail (Hamilton). The production explores memory, mentalism, and audience interaction — inspired by DePonto's grandparents' experiences with memory loss — and had drawn attention from the Washington Post ("enthralling feat of mind-probing prowess"), Broadway World, and the New York Theatre Guide. JJ Abrams attended opening night at the Geffen. With that caliber of producing team, press momentum from three sold-out regional engagements, and a New York premiere at a respected Off-Broadway house, the production needed a digital platform that could convert critical buzz into ticket sales and establish Mindplay's brand identity for its highest-visibility engagement yet.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The site needed to be live ahead of the December 15 first preview and carry the production through its Off-Broadway run at the Greenwich House Theater.</p>
