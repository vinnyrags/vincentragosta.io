<h2 class="wp-block-heading"><span style="text-decoration: underline;">Implementation</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A custom WordPress build with a dark, sophisticated design system and a ticketing calendar tuned for an intimate Off-Broadway house.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>I led development on a custom WordPress theme where the design challenges were the defining feature of the build. Mindplay's brand identity — dark, moody, and precise — demanded a frontend that could carry a sophisticated color palette of deep navy, cream, and gold without sacrificing readability or accessibility. The design system used CSS custom properties to manage the color hierarchy across every component, with responsive typography scaling that maintained the site's elegant proportions from mobile through desktop. The hero section featured a prominent black-and-white portrait of DePonto with the tagline "What's on your mind?" — a composition that needed to feel cinematic on large screens and impactful on phones without cropping away the atmosphere. The ticketing calendar was the primary conversion component — an interactive, date-driven interface rendering the performance schedule at the Greenwich House Theater with availability states and direct purchase links routing audiences into the ticketing provider's checkout flow. Press acclaim from the Washington Post, New York Theatre Guide, Broadway World, and Times Square Chronicles was surfaced through a structured quotes section that reinforced the show's critical momentum. Gravity Forms powered newsletter signup collecting first name, last name, email, and zip code with privacy consent management. YouTube embeds delivered video content, and Schema.org structured data — WebPage, Organization, and BreadcrumbList — provided the SEO foundation for a show entering a competitive Off-Broadway market.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The design work on Mindplay was a satisfying constraint problem — building something that feels mysterious and atmospheric while keeping the code clean, accessible, and performant underneath.</p>
