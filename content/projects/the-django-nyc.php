<h2 class="wp-block-heading"><span style="text-decoration: underline;">Background</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A world-class jazz venue needed a digital presence with the same energy as a live set.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The Django — a subterranean jazz club in the cellar of the Roxy Hotel at 2 Avenue of the Americas in Tribeca — programs live jazz seven nights a week across two nightly sets, drawing both rising talent and established artists to a room modeled after the intimate boîtes of Paris. With vaulted ceilings, exposed brick walls, a state-of-the-art Meyer Sound system, two cocktail bars, and a dinner service anchored by an award-winning cocktail program from mixologist Natasha David, the venue had built a loyal following since opening in 2015. King &amp; Partners, the Roxy Hotel's long-standing agency partner for strategy, branding, and digital, brought me on to lead development on The Django's standalone web platform — a site that needed to capture the energy of the room and convert it into reservations, ticket sales, and private event inquiries.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The site had to serve jazz enthusiasts, casual diners, hotel guests, and event planners — each with different entry points and conversion goals — while establishing The Django as a destination in its own right, distinct from the Roxy Hotel property site.</p>
