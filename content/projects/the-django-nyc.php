<h2 class="wp-block-heading"><span style="text-decoration: underline;">Implementation</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>Extending a proven headless architecture with venue-specific features built for nightly programming.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The platform ran on the same decoupled architecture King &amp; Partners had established across their hospitality portfolio: a Ruby on Rails frontend consuming content from a headless WordPress backend via REST API. I worked within and extended this existing infrastructure — inheriting the content modeling, API layer, and deployment patterns, then building out The Django-specific frontend templates, components, and integrations on top of that foundation. This was the same architectural pattern I built on for the Soho Diner project, also for King &amp; Partners, which meant I brought direct familiarity with the stack and its conventions from day one.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The Rails frontend delivered custom page templates with an event schedule and date-based calendar navigation via Pikaday, image carousels powered by Swiper.js, a 360-degree panoramic venue viewer using Pannellum.js, and a visual identity built in SASS with custom typography — Bernhard, Geometric Heavy, and Tilda Petite typefaces paired against the venue's signature red (#ec4429) and textured paper backgrounds. WordPress Multisite served as the CMS, giving the client full editorial control over the performance schedule, dinner menus, photo galleries, stories, and private event details through a familiar admin interface. Third-party integrations included Resy for table reservations, Contact Form 7 for private event inquiries, and OneTrust for cookie consent management.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The Django operated on its own domain and codebase, distinct from the Roxy Hotel property site, while sharing the underlying WordPress Multisite content infrastructure — the same separation pattern I implemented for Soho Diner within the same engagement.</p>
