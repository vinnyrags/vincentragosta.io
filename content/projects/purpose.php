<h2 class="wp-block-heading"><span style="text-decoration: underline;">Results</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>Tony Award for Best Play, Pulitzer Prize for Drama, $15 million at the box office.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Purpose won the 2025 Tony Award for Best Play, the Pulitzer Prize for Drama, the Drama Desk Award for Outstanding Play, and the New York Drama Critics' Circle Award for Best Play — a clean sweep of every major dramatic honor. Kara Young won Best Featured Actress in a Play. The run averaged 96.55% capacity across 192 performances and 22 previews at the Helen Hayes Theatre, grossing over $15.1 million at an average ticket price of $124.91. The production played its final performance on August 31, 2025. In the same Tony ceremony, Maybe Happy Ending won Best Musical — meaning two of my builds took the top prize in their respective categories on the same night.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">Two builds, two top Tony Awards, one ceremony. That is not something you plan for — but the infrastructure was ready when it happened.</p>
