<h2 class="wp-block-heading"><span style="text-decoration: underline;">Implementation</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A custom WordPress build with a ticketing calendar and frontend layouts that pushed beyond the typical Broadway site.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The frontend was the primary challenge. The design called for layouts that broke from standard Broadway production site conventions — asymmetric grids, overlapping content sections, and typographic treatments that demanded precise CSS positioning across breakpoints. Every layout decision had to hold up on mobile without sacrificing the visual tension that made the desktop compositions compelling. Compare this to the <a href="https://vincentragosta.io/projects/smash/">Smash</a> build happening in the same window: where Smash was cinematic and maximalist, Purpose was architecturally sparse — black, white, and warm grays with a typographic hierarchy built on uppercase headings and generous spacing. The restraint made every element heavier. The ticketing calendar followed the same proven Telecharge integration pattern, and the broader site carried hero video content with YouTube integration, cast and creative team profiles, and press acclaim sections aggregating reviews from the New York Times, Washington Post, Vogue, and the New Yorker.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The comp looked simple — the CSS architecture behind it was anything but. Restraint in design often means more precision in code.</p>
