<h2 class="wp-block-heading"><span style="text-decoration: underline;">Implementation</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A media-dense Broadway build with integrated ticketing, streaming, and education content.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The site was built on WordPress with a content architecture designed for the volume and velocity of a Broadway marketing cycle. The ticketing integration connected to Telecharge with a dynamic performance calendar surfacing real-time availability and "Selling Fast" indicators, alongside premium seating options, group sales through Broadway Inbound, and a GOVX military discount program — each purchase path routed correctly without friction. The Look &amp; Listen section delivered a media-rich experience: production photography, video content, and a stream selector that let visitors listen to the original cast recording — released on Masterworks Broadway/Sony Masterworks — across Spotify, Apple Music, or Amazon Music without leaving the site. Beyond the standard Broadway site scope, the build included an education toolkit with downloadable study guides and "Know Before You Go" materials, turning the site into a resource for schools and group organizers bringing students to the theatre.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">Every content surface was built for the marketing team to manage independently — press quotes, cast updates, ticket promotions, and media assets all flow through the CMS without developer dependency.</p>
