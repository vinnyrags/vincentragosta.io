<h2 class="wp-block-heading"><span style="text-decoration: underline;">Implementation</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A media-dense Broadway build with integrated ticketing, streaming, and education content.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The Telecharge ticketing integration followed the mature pattern I had refined across many previous Broadway builds — performance calendar, availability states, premium seating, group sales through Broadway Inbound, GOVX military discounts — so the infrastructure stood up quickly and reliably. What made this build distinctive was the content layer beyond ticketing. The Look &amp; Listen section delivered a stream selector that let visitors listen to the original cast recording — released on Masterworks Broadway/Sony Masterworks — across Spotify, Apple Music, or Amazon Music without leaving the site. More unusually, the build included a full education toolkit with downloadable study guides and "Know Before You Go" materials, turning the site into an active resource for schools and group organizers bringing students to the theatre. That education component gave the site a depth most Broadway builds do not require.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The education toolkit was unique in my Broadway portfolio — most shows market to theatergoers, but The Outsiders also had to serve teachers, students, and school group coordinators.</p>
