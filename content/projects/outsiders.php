<h2 class="wp-block-heading"><span style="text-decoration: underline;">Background</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A new musical adapted from an American classic needed a digital platform built for a Broadway premiere.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The Outsiders — a new musical based on S.E. Hinton's 1967 novel and Francis Ford Coppola's 1983 film — was transferring to Broadway after a world premiere run at La Jolla Playhouse. The production featured a book by Adam Rapp with Justin Levine, music and lyrics by Jamestown Revival (Jonathan Clay and Zach Chance) alongside Levine, and direction by Danya Taymor. With Coppola himself as a producer, twelve Tony nominations ahead of it, and a built-in audience spanning generations of readers and filmgoers, the show was one of the most anticipated new musicals of the 2023-24 season. Previews were set to begin March 16, 2024 at the Bernard B. Jacobs Theatre, with an official opening on April 11. By this point my Broadway ticketing and tour infrastructure was thoroughly battle-tested across years of productions — the challenge here was less about inventing patterns and more about building the right content architecture for a show with unusually deep source material and an education program targeting schools nationwide.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">Unlike most new musicals, The Outsiders arrived with a massive existing audience — the platform needed to convert decades of cultural recognition into ticket purchases.</p>
