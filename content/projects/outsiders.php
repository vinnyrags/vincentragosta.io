<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>Four Tony Awards, a full recoupment, and a national tour — all running through the same platform.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The Outsiders won four Tony Awards including Best Musical and Best Direction for Danya Taymor — making it the fourth consecutive year I built the digital home for Broadway's top prize, following Kimberly Akimbo in 2023. The platform handled the sustained demand that followed the wins: the production recouped its full investment by December 2025, and a North American tour launched in September 2025, opening at the Tulsa Performing Arts Center — the show's hometown and the setting of the story itself. The site expanded to support tour content and routing alongside the ongoing Broadway run, with the marketing team managing both properties through the same CMS. A year later, Maybe Happy Ending would win Best Musical for 2025, extending the streak to five.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">Five Best Musical Tony winners across my portfolio is not a stat I could have predicted when Dear Evan Hansen launched — but each one built on the infrastructure and instincts of the last.</p>
