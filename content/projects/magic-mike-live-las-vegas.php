<h2 class="wp-block-heading"><span style="text-decoration: underline;">Background</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A hit Las Vegas show was relocating to a new venue and needed a digital relaunch to match.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Magic Mike Live — created and co-directed by Channing Tatum, produced by Base Entertainment — first opened in Las Vegas in April 2017 at the Hard Rock Hotel, where it ran for over 1,300 performances across more than two years. When the Hard Rock closed to reopen as Virgin Hotels Las Vegas, the production secured a new permanent home at SAHARA Las Vegas on the Strip. The move was originally planned for spring 2020, but the COVID-19 pandemic pushed the opening twice — first to March 2021, then to August 26, 2021. That delay created a rare window: a proven production with an established audience relaunching in a new venue, needing a digital platform that could reintroduce the show to the market and drive ticket sales from day one of the reopening.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The site needed to sell tickets across multiple tiers — general admission, VIP experiences, and group bookings of 10 or more — while conveying the energy and premium positioning of a 90-minute, 360-degree live entertainment experience.</p>
