<h2 class="wp-block-heading"><span style="text-decoration: underline;">Background</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A hit Las Vegas show was relocating to a new venue and needed a digital relaunch to match.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>This was my first project outside the Broadway ecosystem. Magic Mike Live — created and co-directed by Channing Tatum, produced by Base Entertainment — had already logged over 1,300 performances at the Hard Rock Hotel before the venue closed. The production secured a new permanent home at SAHARA Las Vegas on the Strip, but the COVID-19 pandemic pushed the reopening twice — first to March 2021, then to August 26, 2021. A Las Vegas residency operates under fundamentally different rules than a Broadway run: the audience is transient rather than local, the purchase decision often happens same-day, and the competition is not other shows but every other experience on the Strip. The platform needed to sell a 90-minute, 360-degree live entertainment experience to people who might be choosing between this and a steakhouse reservation.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The conversion psychology here was different from Broadway — less "plan ahead for a special occasion" and more "decide tonight, buy now, arrive in an hour."</p>
