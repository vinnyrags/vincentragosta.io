<p>The production surpassed 2,000 performances at SAHARA alone in March 2026 and has exceeded 3,300 total Las Vegas performances since 2017 — making it one of the longest-running entertainment experiences on the Strip. The show expanded to additional markets including New York and London, with the site architecture supporting multi-location content alongside the Las Vegas-specific experience. Building for a Vegas residency gave me a different set of instincts than the Broadway work — faster conversion funnels, more aggressive calls-to-action, and an understanding of how venue partnerships shape the digital experience. Those instincts informed how I thought about ticketing architecture on subsequent builds.</p>
