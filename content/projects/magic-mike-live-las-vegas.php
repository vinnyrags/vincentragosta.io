<h2 class="wp-block-heading"><span style="text-decoration: underline;">Implementation</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A conversion-driven WordPress build with tiered ticketing and a frontend built for energy.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>I led development on a custom WordPress site designed around a single priority: moving visitors from landing to ticket purchase with minimal friction. The homepage was built hero-first with prominent calls-to-action routing visitors into three distinct ticket pathways — general admission, VIP experiences ("Mike's Ultimate Experience"), and group sales with a 10+ discount incentive. The ticketing integration connected to the TickXCore widget, embedding the purchase flow directly into the site rather than redirecting to a third-party platform. The frontend leaned into the show's visual identity with a dark color scheme, CSS clip-path animations on buttons, and responsive imagery optimized across multiple breakpoints. I integrated the Lenis library for smooth scrolling behavior and built a media carousel showcasing press quotes from outlets like Glamour, PopSugar, and the Las Vegas Sun. The site also supported cross-promotional content for SAHARA Las Vegas — hotel booking links and dining reservations surfaced contextually alongside show information.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">Every page enforced an 18+ age requirement and valid photo ID policy, with accessibility features including screen reader detection, reduced-motion support, and keyboard navigation optimization built into the frontend from the start.</p>
