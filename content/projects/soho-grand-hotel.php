<h2 class="wp-block-heading"><span style="text-decoration: underline;">Implementation</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>Extending an established headless architecture to serve a flagship hospitality brand.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>King &amp; Partners had built a decoupled architecture for their GrandLife Hotels portfolio that separated concerns cleanly: a Ruby on Rails application handled routing, page rendering, and frontend presentation, while a headless WordPress backend managed all content through the REST API. WordPress Multisite unified the CMS layer across properties, giving each brand — Soho Grand, Soho Diner, The Django — its own editorial workspace while sharing the underlying content infrastructure. I worked within and extended this existing stack, inheriting the content modeling, API conventions, and deployment patterns, then building out Soho Grand-specific templates, components, and integrations on top of that foundation.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The Rails frontend delivered responsive page templates for rooms, suites, dining venues, event spaces, and a gallery section — each with its own content structure and visual treatment. The design system was built in SASS with custom typography and a photography-forward layout that reflected the hotel's design heritage. Third-party integrations included booking engine widgets for room reservations, Resy for restaurant bookings, and event inquiry forms for the hotel's private event spaces. The content layer gave the client's team full editorial control over room details, seasonal promotions, dining menus, and cultural programming.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">As the flagship property, the Soho Grand site set the standard that informed the Soho Diner and Django builds — each running its own domain and frontend while drawing from the same multisite CMS backend.</p>
