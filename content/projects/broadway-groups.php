            <h2 class="wp-block-heading"><strong>A custom WordPress build with a multi-step inquiry system, a deep data model for 40+ shows, and a season lifecycle architecture designed for continuous operation.</strong></h2>
            <!-- /wp:heading -->

            <!-- wp:paragraph -->
            <p>I led development on a custom WordPress theme where the data model was the defining architectural challenge. Every show on the platform carried structured data across multiple dimensions — venue, seating capacity, performance schedule, group pricing tiers, minimum group sizes, age appropriateness, genre classification, and availability status — all managed through a dedicated content architecture that the sales team could maintain without developer involvement. The inquiry form was the platform's primary conversion mechanism and the most complex form system I had built to that point: a multi-step Gravity Forms implementation that collected group type, preferred shows, date flexibility, group size, budget parameters, and contact information, then routed submissions to the appropriate sales representatives based on inquiry characteristics. The form logic handled conditional display rules, validation across steps, and submission routing that mapped to the internal team's workflow. The season lifecycle architecture addressed the operational reality that Broadway's programming turns over continuously — shows open, close, extend, and adjust schedules throughout the year. The content layer was designed so the team could onboard new productions, retire closed shows, and update availability states across the entire catalog without structural changes to the site. Show detail pages featured seating charts for Broadway's 40+ theaters, educator resources including study guides through the Broadway Classroom program, and audience-specific landing pages for social groups, military organizations, schools, corporate clients, and tour operators. The frontend delivered a responsive experience optimized for the platform's distinct user segments — from a tour operator booking a season's worth of performances to a teacher researching a single field trip.</p>
            <!-- /wp:paragraph -->

            <!-- wp:paragraph {"className":"is-style-muted"} -->
            <p class="is-style-muted">The inquiry form and the data model were inseparable challenges — the form had to be smart enough to surface relevant options from a catalog of 40+ shows, and the data model had to be structured enough to power that intelligence without custom code for every new production.</p>
