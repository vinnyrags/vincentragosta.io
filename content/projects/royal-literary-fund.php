<h2 class="wp-block-heading"><span style="text-decoration: underline;">Background</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>One of the oldest literary charities in the world needed a digital platform worthy of its mission.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The Royal Literary Fund was founded in 1790 by Reverend David Williams — inspired by the death in debtors' prison of Floyer Sydenham, a translator of Plato — and has operated continuously for over 230 years under a Royal Charter granted in 1842. The Fund supports professional writers facing financial hardship through direct grants and, since 1999, places experienced writers as Fellows at over 100 UK universities to provide free writing support to students and staff. Its beneficiaries have included Joseph Conrad, D.H. Lawrence, James Joyce, and Ivy Compton-Burnett. The existing website had fallen behind the organization's scope — the fellows directory was difficult to navigate, the grants program was hard to find, and the archival content that represented centuries of literary history lacked a coherent digital home. Situation Interactive was engaged to redesign and rebuild the platform from discovery through launch.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The site needed to serve three distinct audiences — writers seeking financial support, universities hosting Fellows, and the broader literary community engaging with the Fund's editorial and historical content.</p>
