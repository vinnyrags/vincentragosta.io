<h2 class="wp-block-heading"><span style="text-decoration: underline;">Background</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>New Jersey's anchor cultural institution needed a digital experience to match its scale.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The New Jersey Performing Arts Center — opened in 1997 in downtown Newark — is one of the largest performing arts centers in the United States, hosting performances across Prudential Hall (2,868 seats), the Victoria Theater (511 seats), and multiple event and education spaces. With over 12 million visitors since opening, NJPAC serves as both the state's premier arts venue and a catalyst for economic development in Newark. The existing website had fallen behind the organization's ambitions — event discovery was cumbersome, the design felt dated against peer institutions, and the ticketing integration created friction between browsing and purchasing. Situation Interactive was engaged to redesign and rebuild the entire digital platform from the ground up.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The scope was institutional-grade: hundreds of events per season across multiple venues, arts education programs, donor pathways, group sales, venue rentals, and bilingual English/Spanish content — all funneling toward ticket conversion.</p>
