<h2 class="wp-block-heading"><span style="text-decoration: underline;">Implementation</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A comprehensive design system, deep ticketing integration, and a slider built to set the standard.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>I led development on a custom WordPress theme built to support the full complexity of a major performing arts institution. The design system was one of the most comprehensive I have worked on — a modular library of reusable components covering event cards, venue maps, program grids, donor CTAs, calendar views, and editorial layouts, all governed by consistent spacing, typography, and color tokens. Every component was built responsive-first and stress-tested across the full range of content scenarios the CMS would produce. The Ticketmaster API integration connected event listings directly to the purchasing flow, with deep links routing visitors from any event card or detail page straight into the ticketing platform with the correct show, date, and venue pre-selected — eliminating the dead-end search experience that had plagued the previous site.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The homepage hero slider was a standout piece of engineering — a custom-built carousel with layered animations, staggered content transitions, and precise timing controls that delivered the visual impact the design demanded without sacrificing performance or accessibility. It remains one of the most intricate slider implementations I have ever built, and one of the UI components I am most proud of across my entire career.</p>
