<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A two-year delay made a star-powered revival even higher-stakes.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>American Buffalo — David Mamet's 1975 play about loyalty, greed, and a botched heist in a Chicago junk shop — was set to return to Broadway in spring 2020 with Laurence Fishburne, Sam Rockwell, and Darren Criss at the Circle in the Square Theatre. Then Broadway shut down on March 12, 2020. The production announced its return in October 2021 with the same cast and director Neil Pepe intact, setting an opening night of April 14, 2022 — exactly two years to the day after the original planned opening. The fact that an Oscar winner, an Emmy winner, and a Golden Globe winner all held their commitments through a two-year pandemic delay was itself remarkable.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">Situation Interactive handled the digital marketing. I led development on the official site ahead of the 16-week limited engagement.</p>
