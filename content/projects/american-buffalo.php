<h2 class="wp-block-heading"><span style="text-decoration: underline;">Implementation</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A ticketing-focused build with structured cast content for a three-person powerhouse.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The site was built on WordPress with a custom theme tailored to the production's visual identity — bold orange and gray tones with classic theatrical typography that matched the show's gritty, no-nonsense marketing. The primary deliverable was a ticketing calendar integrated with Broadway.com and premium seat vendors, giving visitors a date-driven interface to browse the 16-week limited run and purchase directly. For a limited engagement with star-driven demand, the ticketing flow needed to be frictionless — every extra click was a potential lost sale.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The cast and creative section was built as a structured content system with individual bios, headshots, and role assignments managed through dedicated field groups. With only three actors but a deep creative team — director Neil Pepe, scenic designer Scott Pask, costume designer Dede Ayite, lighting designer Tyler Micoleau — the content layer needed to showcase both the star power and the production craft. The site also featured critical praise sections, media galleries, and newsletter signup, all built for the marketing team to manage independently through the CMS.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The Circle in the Square's thrust configuration made the production intimate and intense — the site's design reflected that same directness.</p>
