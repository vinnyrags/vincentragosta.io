<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A two-year delay made a star-powered revival even higher-stakes.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>David Mamet's 1975 play about loyalty, greed, and a botched coin heist in a Chicago junk shop was set to return to Broadway in spring 2020 with Laurence Fishburne, Sam Rockwell, and Darren Criss. Then Broadway shut down on March 12, 2020. The production held together through the entire closure — same cast, same director Neil Pepe — and announced its return for April 2022 at the Circle in the Square Theatre, the same thrust-stage house where Oklahoma! had staged its acclaimed revival run. Opening night landed on April 14, exactly two years to the day after the original target. The fact that all three leads honored two-year-old commitments through a pandemic was itself a story — and it generated the kind of advance press that demands a site ready to convert from day one.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">This was one of three productions I launched within the same two-week window in spring 2022, alongside for colored girls and The Minutes — a concentrated stretch that pressure-tested every workflow.</p>
