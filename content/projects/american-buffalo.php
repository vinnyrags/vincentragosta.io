<h2 class="wp-block-heading"><span style="text-decoration: underline;">Implementation</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A ticketing-focused build with structured cast content for a three-person powerhouse.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The custom WordPress theme leaned into the production's visual identity — bold orange and gray, stripped-down theatrical typography, no ornamentation. Everything about the show was spare and direct, and the site followed suit. The ticketing calendar served as the primary conversion surface, integrating with Broadway.com and premium seat vendors to give visitors a date-driven interface across the 16-week window. With star-driven demand and a hard closing date, the purchase flow had to be frictionless.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Cast and creative content took a different shape here than on larger-ensemble builds. Three actors meant three bios front and center, but the creative team behind them — director Neil Pepe, scenic designer Scott Pask, costume designer Dede Ayite, lighting designer Tyler Micoleau — warranted equal presence. Structured field groups let the marketing team manage all profiles, press quotes, and media galleries without touching code. The Circle in the Square's thrust configuration gave the production an immediacy that the site's layout reflected: tight spacing, direct messaging, nothing between the visitor and the ticket.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The compact three-person cast made the content architecture lighter than something like for colored girls, which had seven performers each requiring distinct visual treatment — but the ticketing pressure was identical.</p>
