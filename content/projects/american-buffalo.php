<h2 class="wp-block-heading"><span style="text-decoration: underline;">Results</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>Four Tony nominations, $8.4 million at the box office, and a complete run.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The site launched ahead of the first preview on March 22, 2022 and served as the production's digital hub through all 101 performances and four Tony Award nominations — Best Revival of a Play, Best Leading Actor (Sam Rockwell), Best Direction (Neil Pepe), and Best Scenic Design (Scott Pask). The production grossed $8.4 million across its 16-week limited engagement at the Circle in the Square Theatre, with preview-period attendance reaching 94% of capacity.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The ticketing calendar and cast content system ran the full lifecycle of the engagement without modification. The marketing team managed all post-launch updates — Tony nomination badges, critical praise additions, schedule changes — entirely through the CMS with no developer support. The structured content architecture and ticketing patterns built for this production carried directly into subsequent Broadway builds at Situation Interactive.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The development patterns established on this project — ticketing calendar, cast content system, limited-run lifecycle management — became reusable components across future theatrical builds.</p>
