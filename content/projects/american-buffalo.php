<h2 class="wp-block-heading"><span style="text-decoration: underline;">Results</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>Four Tony nominations, $8.4 million at the box office, and a complete run.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The site launched ahead of previews on March 22, 2022 and ran through all 101 performances. American Buffalo earned four Tony nominations — Best Revival of a Play, Best Leading Actor (Sam Rockwell), Best Direction (Neil Pepe), and Best Scenic Design (Scott Pask) — and grossed $8.4 million across its limited engagement, with preview-period attendance at 94% of capacity.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Nothing about the site required modification after launch. The ticketing calendar, cast profiles, and content management layer all held through the full lifecycle — Tony nomination badges, critical praise additions, and schedule changes were handled entirely through the CMS. Launching this site in the same compressed window as for colored girls and The Minutes validated that the underlying architecture could be adapted quickly across very different productions without starting from scratch each time.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The ticketing and cast content patterns from this build carried directly into <a href="https://vincentragosta.io/projects/topdog-underdog/">Topdog/Underdog</a> and the larger musicals that followed later in 2022.</p>
