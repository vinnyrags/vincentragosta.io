<h2 class="wp-block-heading"><span style="text-decoration: underline;">Implementation</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>Dynamic search, structured roster data, and a contact system engineered for precision routing.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>I led development on a custom WordPress theme built around two core systems. The first was the artist search — a dynamic, JavaScript-driven interface that lets users filter and search across the full roster by name, discipline, instrument, and category. The roster spans 12+ taxonomies including conductors, instrumentalists (broken out by piano, violin, cello, guitar, organ, and more), vocalists, chamber music, dance and theater, jazz and global music, symphony pops, films with orchestra, and contemporary. Each artist profile is a structured content object with biographical data, media assets, tour schedules, and management relationships, all managed through WordPress custom post types and field groups. The second core system was the contact architecture — an intricate routing layer that maps every artist to their specific agent or management team, with inquiry forms that dynamically adjust based on the artist, discipline, and office location. A presenter inquiring about booking a conductor in North America reaches a different agent than one inquiring about a jazz ensemble for a European tour. That routing logic is invisible to the user but critical to the agency's operations.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The content architecture gave the Opus 3 team full control over roster updates, artist profiles, news publishing, and contact relationships — all manageable through the WordPress admin without developer involvement.</p>
