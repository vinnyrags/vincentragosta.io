<h2 class="wp-block-heading"><span style="text-decoration: underline;">Implementation</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>Three venues, three production phases, one platform — with the tour system as the defining technical challenge.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The ticketing calendar and cast profile patterns were mature by this point. What made Suffs distinct was the scope of evolution the platform had to support. The calendar adapted across three venue contexts — the Newman Theater at The Public, the Music Box Theatre on Broadway, and every tour stop from Seattle to Chicago — each with different ticketing providers and schedule structures. When the national tour launched in Fall 2025, the platform expanded to include a city-by-city tour system with venue information, local ticketing endpoints, and tour-specific content across 20+ markets, all managed through the CMS.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The cast section carried particular depth given the entirely female and non-binary cast: structured profiles for Taub, James, Colella, Skinner, Grace McLean, Hannah Cruz, and the full ensemble that updated seamlessly as casting evolved between The Public, Broadway, and tour companies. The site also served as the hub for press coverage, streaming links for the Grammy-nominated cast recording, and merchandise — reflecting the show's unusually broad cultural footprint for a new musical.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The same architecture that powered The Public Theater premiere scaled through Broadway and a national tour without a rebuild.</p>
