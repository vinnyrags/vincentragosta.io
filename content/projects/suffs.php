<h2 class="wp-block-heading"><span style="text-decoration: underline;">Background</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A politically charged original musical with a production arc spanning three distinct phases.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Suffs — Shaina Taub's original musical with book, music, and lyrics all by Taub — tells the story of the American women's suffrage movement through the debates, alliances, and fractures among activists like Alice Paul, Carrie Chapman Catt, Ida B. Wells, and Lucy Burns. The show premiered Off-Broadway at The Public Theater's Newman Theater in March 2022, directed by Leigh Silverman, and generated enough critical and commercial momentum to make a Broadway transfer inevitable. When producers Jill Furman and Rachel Sussman announced the Broadway production at the Music Box Theatre in October 2023 — with Hillary Clinton and Malala Yousafzai joining as producers in their Broadway debuts — the show's political resonance became inseparable from its marketing identity. The entirely female and non-binary cast featured Tony winner Nikki M. James as Ida B. Wells, Tony nominee Jenn Colella as Carrie Chapman Catt, and Tony nominee Emily Skinner as Alva Belmont.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The site launched for The Public Theater premiere and needed to carry the production through Off-Broadway, Broadway, and a national tour — one of the longest continuous platform lifecycles in this portfolio.</p>
