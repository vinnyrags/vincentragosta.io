<h2 class="wp-block-heading"><span style="text-decoration: underline;">Results</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>Two Tony Awards, a Grammy nomination, a national tour across 20+ cities, and a platform that carried the production from its first Off-Broadway preview to a PBS filmed recording.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The site launched ahead of the first preview at The Public Theater on March 13, 2022. The Broadway run at the Music Box Theatre — 24 previews and 301 performances from March 26, 2024, through January 5, 2025 — earned six Tony nominations and won two: Best Book of a Musical and Best Original Score, both for Shaina Taub, making her the first woman to independently win both awards in the same season. Three Outer Critics Circle Awards followed, including Outstanding New Broadway Musical, along with a Grammy nomination for Best Musical Theater Album and a PBS Great Performances filmed recording. The tour system scaled to support the national tour launching in Fall 2025 across 20+ cities — from the 5th Avenue Theatre in Seattle through the CIBC Theatre in Chicago and the National Theatre in Washington, D.C.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">Four years and counting on a single codebase — Off-Broadway, Tony-winning Broadway run, Grammy-nominated album, PBS recording, and a national tour. The site remains live at suffsmusical.com.</p>
