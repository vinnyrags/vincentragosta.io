<p>The new platform launched ahead of Roundabout's 2025-26 season — a year featuring the first Broadway revival of The Rocky Horror Show in 24 years at Studio 54, the premiere of Chinese Republicans Off-Broadway, and the reopening of the newly renovated Todd Haimes Theatre with Fallen Angels starring Kelli O'Hara and Rose Byrne. The content team managed the full season rollout — show pages, subscription campaigns, venue updates reflecting the renovation — independently through the block editor, with zero developer support required for editorial operations. The structured content architecture handled the complexity of multi-venue scheduling, cross-linked education programming, and donor communications without the bottlenecks that had plagued the previous platform.</p>
