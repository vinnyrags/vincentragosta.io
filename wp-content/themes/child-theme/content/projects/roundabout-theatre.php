<h2 class="wp-block-heading"><span style="text-decoration: underline;">Implementation</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>Hybrid Full Site Editing — Gutenberg and Twig working together through theme.json.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The platform was built on WordPress using a hybrid FSE approach — leveraging theme.json as the single source of truth for design tokens (typography scales, color palettes, spacing presets, layout constraints) while rendering frontend templates through Twig rather than PHP block templates. This gave the editorial team the full power of the Gutenberg block editor for content assembly while keeping the frontend rendering layer clean, composable, and testable. The parent-child theme architecture used a provider pattern to organize features into self-contained domains — each owning its own blocks, assets, and configuration. Show pages, venue pages, education content, and donation flows each operated as independent content models with structured field groups, connected through relational queries that surfaced contextual cross-links automatically. The block editor configuration was tuned for Roundabout's specific editorial needs: curated block palettes, locked layout patterns for show pages, and reusable template parts that enforced brand consistency without restricting the content team's ability to compose pages for each new season.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The architectural decisions made on this project — the hybrid FSE model, the provider pattern, the theme.json-driven design system — became the foundation for the platform I now use across every client engagement in my independent practice.</p>
