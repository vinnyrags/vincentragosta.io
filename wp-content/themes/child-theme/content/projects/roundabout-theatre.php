<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A nearly 60-year-old institution needed a digital platform built for the next decade.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Roundabout Theatre Company — founded in 1965, operating five New York City venues including the Todd Haimes Theatre, Studio 54, and the Stephen Sondheim Theatre — had outgrown its existing website. The organization produces a full season of Broadway and Off-Broadway plays and musicals, runs education programs reaching thousands of students, manages complex ticketing across multiple venues, and maintains donor and subscription relationships at institutional scale. The existing site couldn't keep up with the content demands of a modern season cycle: show pages, event campaigns, education programming, donor communications, and accessibility information all competing for attention on aging infrastructure. Situation Interactive, Roundabout's digital agency of record, brought me on to lead the frontend build of a ground-up platform replacement.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">This wasn't a redesign — it was a complete re-architecture, replacing legacy systems with a modern WordPress platform built to handle the operational complexity of one of Broadway's most active producers.</p>
