        <!-- wp:column -->
        <div class="wp-block-column"><!-- wp:heading -->
            <h2 class="wp-block-heading"><strong>A media-rich Broadway build with integrated ticketing and a multi-platform stream selector.</strong></h2>
            <!-- /wp:heading -->

            <!-- wp:paragraph -->
            <p>The site was built on WordPress with a content architecture designed around the density of a Broadway marketing cycle — production photos, video embeds, cast bios, press quotes, FAQs, and ticketing all competing for attention across a compact page structure. The ticketing integration connected directly to Telecharge, with a performance calendar surfacing availability, cast alternates by date, and multiple purchase paths: standard tickets, premium seating, the Club 2064 digital lottery at $20.64 per ticket, in-person rush at $49, and group sales through Broadway Inbound. The Look & Listen section featured a tabbed media gallery — photos, videos, and music — with the music tab powered by a custom stream selector that dynamically loads embedded players from Spotify, Apple Music, or Amazon Music based on user selection, routing listeners to the original cast recording on their preferred platform without leaving the page.</p>
            <!-- /wp:paragraph -->

            <!-- wp:paragraph {"className":"is-style-muted"} -->
            <p class="is-style-muted">Every content surface was built for the marketing team to update independently — new press quotes, cast announcements, video drops, and ticket promotions could all be managed through the CMS without developer involvement.</p>
