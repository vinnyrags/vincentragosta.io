<h2 class="wp-block-heading"><span style="text-decoration: underline;">Background</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>Broadway's largest nonprofit wanted to own its ticketing experience — not outsource it.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Roundabout Theatre Company operates three Broadway venues — the Todd Haimes Theatre, Studio 54, and the Stephen Sondheim Theatre — and regularly rents them to commercial productions. For years, ticketing for those rental shows funneled through external sellers, meaning Roundabout had no control over the purchase experience, no direct relationship with the ticket buyer, and no visibility into the data. With support from Bloomberg Philanthropies' Digital Accelerator for Arts and Culture — a $128 million program launched in 2021 to strengthen digital infrastructure at nonprofit cultural organizations — Roundabout partnered with Situation Interactive to build something that didn't exist: a proprietary ticketing platform purpose-built for Broadway venue operators.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The project came out of Situation's Hatch innovation program — nine months of research and development focused on rethinking the user experience of buying a Broadway ticket from the ground up.</p>
