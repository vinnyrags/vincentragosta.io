<h2 class="wp-block-heading"><span style="text-decoration: underline;">Results</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>Webby-honored, processing real Broadway transactions from day one.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Criterion launched in July 2022 with & Juliet as its first production, processing primary ticket sales across Roundabout's three Broadway venues from day one. The platform gave Roundabout something no external ticketing vendor could: direct ownership of the patron relationship, full visibility into purchase data, and the ability to integrate programs like HipTix — their $30 ticket initiative for theatregoers ages 18-40 — natively into the purchase flow rather than as a bolt-on afterthought. The platform expanded to include native iOS and Android apps, extending the same typed architecture to mobile. In 2024, Criterion was recognized as a Webby Award Honoree in the Best User Experience category — validation from the International Academy of Digital Arts and Sciences that the UX investment paid off.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">Criterion remains in active production, processing ticket sales for every rental production across Roundabout's Broadway houses — a platform built once and scaled to handle the full volume of one of New York's busiest theatre operations.</p>
