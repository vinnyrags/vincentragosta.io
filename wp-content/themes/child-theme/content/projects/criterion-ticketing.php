<h2 class="wp-block-heading"><span style="text-decoration: underline;">Implementation</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A typed frontend architecture built to model the complexity of Broadway ticketing.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The platform was built as a TypeScript and Vue.js application — a deliberate departure from the WordPress and PHP stack that dominated the rest of my work at Situation. The ticketing domain demanded it: seat maps, pricing tiers, discount codes, accessibility accommodations, performance schedules, and venue configurations all intersected in ways that required a rigorous type system to keep under control. Every entity in the ticketing flow — shows, performances, seat sections, price levels, discount applications — was modeled as a typed interface, with the API layer enforcing contracts between the frontend and the ticketing backend. The result was a purchase experience that guided users from show discovery through seat selection to checkout with zero ambiguity at each step, handling the edge cases that plague ticketing UX: holds expiring, sections selling out mid-session, dynamic pricing shifts, and discount code validation against specific performance dates.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The type system wasn't overhead — it was the reason the platform could move fast without breaking. Every API response, every state transition, every user interaction was validated at compile time, not discovered in production.</p>
