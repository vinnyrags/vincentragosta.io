        <div class="wp-block-column"><!-- wp:heading -->
            <h2 class="wp-block-heading"><strong>A custom WordPress build with a ticketing calendar, a full tour system, and structured cast profiles for a production that grew across three engagements.</strong></h2>
            <!-- /wp:heading -->

            <!-- wp:paragraph -->
            <p>I led development on a custom WordPress theme that served as the production's complete digital platform from The Public Theater through Broadway and a national tour. The ticketing calendar was the primary conversion driver — an interactive, date-based interface rendering the full performance schedule with matinee and evening slots, availability states, and direct purchase links routing audiences into the ticketing provider's checkout flow with the correct performance pre-selected. The system adapted across venues: the Newman Theater at The Public, the Music Box Theatre on Broadway, and every tour stop from Seattle to Chicago. When the national tour launched in Fall 2025, the platform expanded to include a full tour system — a city-by-city schedule with venue information, local ticketing endpoints, and tour-specific content, all managed through a structured content layer that let the production team add cities and update dates across 20+ markets without developer involvement. The cast and creative section carried the full depth of a production with an entirely female and non-binary cast: structured biographical profiles for Taub, James, Colella, Skinner, Grace McLean, Hannah Cruz, and the full ensemble, alongside the complete creative team from Silverman through every designer — each managed through a dedicated content architecture that updated seamlessly as casting evolved between the Public Theater, Broadway, and tour companies. Gravity Forms powered newsletter signup and audience engagement, and the site served as the hub for press coverage, media assets, streaming links for the Grammy-nominated cast recording, and merchandise integration.</p>
            <!-- /wp:paragraph -->

            <!-- wp:paragraph {"className":"is-style-muted"} -->
            <p class="is-style-muted">The platform was built to grow with the production — the same architecture that powered The Public Theater premiere scaled to carry a Tony-winning musical through Broadway and a national tour without a rebuild.</p>
