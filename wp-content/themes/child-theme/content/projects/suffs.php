            <h2 class="wp-block-heading"><strong>An original musical about the fight for women's suffrage was heading to Broadway — with Hillary Clinton and Malala Yousafzai producing.</strong></h2>
            <!-- /wp:heading -->

            <!-- wp:paragraph -->
            <p>Suffs — Shaina Taub's original musical with book, music, and lyrics all by Taub — tells the story of the American women's suffrage movement through the internal debates, alliances, and fractures among activists like Alice Paul, Carrie Chapman Catt, Ida B. Wells, and Lucy Burns in the years leading up to the ratification of the 19th Amendment in 1920. The show premiered Off-Broadway at The Public Theater's Newman Theater in March 2022, directed by Leigh Silverman with choreography by Mayte Natalio, scenic design by Riccardo Hernandez, costume design by Tony winner Paul Tazewell, lighting by Lap Chi Chu, and sound by Jason Crystal. The Public Theater run — extended through May 29 — generated the kind of critical and commercial momentum that made a Broadway transfer inevitable. In October 2023, producers Jill Furman and Rachel Sussman announced the Broadway production at the Music Box Theatre, with Hillary Clinton and Malala Yousafzai joining as producers in their Broadway debuts. Taub would star as Alice Paul alongside an entirely female and non-binary cast featuring Tony winner Nikki M. James as Ida B. Wells, Tony nominee Jenn Colella as Carrie Chapman Catt, and Tony nominee Emily Skinner as Alva Belmont. The production needed a digital platform that could establish the show's identity from its earliest days, drive ticket sales across multiple engagements, and scale to support a national tour.</p>
            <!-- /wp:paragraph -->

            <!-- wp:paragraph {"className":"is-style-muted"} -->
            <p class="is-style-muted">The site needed to launch ahead of The Public Theater premiere and carry the production through every phase — Off-Broadway, Broadway at the Music Box Theatre, and a national tour across 20+ cities.</p>
