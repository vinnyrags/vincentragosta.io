            <!-- /wp:heading -->

            <!-- wp:paragraph -->
            <p>The site launched ahead of Suffs' first preview at The Public Theater on March 13, 2022, and has served as the production's primary digital platform through every phase of its life. The Broadway production at the Music Box Theatre ran from March 26, 2024, through January 5, 2025 — 24 previews and 301 performances — earning six Tony Award nominations and winning two: Best Book of a Musical and Best Original Score, both for Shaina Taub, making her the first woman to independently win both awards in the same season. The production also won three Outer Critics Circle Awards including Outstanding New Broadway Musical, received a Grammy nomination for Best Musical Theater Album, and a PBS Great Performances filmed recording. The ticketing calendar drove conversions throughout the Broadway run, and the tour system scaled to support the national tour launching in Fall 2025 across 20+ cities — from the 5th Avenue Theatre in Seattle through the CIBC Theatre in Chicago and the National Theatre in Washington, D.C. The platform I built carried Suffs from an Off-Broadway premiere at The Public Theater through a Tony-winning Broadway run, a Grammy-nominated cast album, and a national tour — and the site remains live at suffsmusical.com as the digital home for one of the most significant new American musicals of the decade.</p>
            <!-- /wp:paragraph -->

            <!-- wp:paragraph {"className":"is-style-muted"} -->
            <p class="is-style-muted">Suffs is the rare project where the platform had to evolve across three distinct production phases — and the architecture I built at The Public Theater in 2022 proved durable enough to carry a Tony-winning musical through Broadway and a national tour without a rebuild.</p>
