<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A cannabis beverage brand was outgrowing its digital presence.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Pamos launched in 2021 as a Los Angeles-based cannabis spirits brand, selling THC-infused cocktails through licensed dispensaries and same-day delivery. When the 2018 Farm Bill opened the door for hemp-derived Delta-9 THC products to ship nationwide, the company pivoted hard — moving from state-licensed cannabis retail into mainstream alcohol distribution channels with partners like ABC Fine Wine & Spirits, Spec's, and Total Wine. That pivot meant the website needed to evolve from a regional brand page into a full national e-commerce platform with retail discovery, age-gated purchasing, and a responsive experience that could hold up across every device and screen size hitting their growing traffic.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The brand was scaling fast — rolling into 20+ states with retail endcaps, cooler placements, and a direct-to-consumer shipping operation — and the site needed to keep pace.</p>
