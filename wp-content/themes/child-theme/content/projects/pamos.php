<h2 class="wp-block-heading"><span style="text-decoration: underline;">Implementation</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>Responsive redesign and a store locator built for a brand going national.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The core of the work was a responsive overhaul — rethinking layouts, typography, and interaction patterns so the site performed cleanly from desktop down to mobile, where the majority of their traffic landed. Product pages, cocktail recipe content, and the checkout flow all needed to feel native on a phone screen without losing the premium visual identity the brand had established. Beyond the responsive work, I built a custom store locator powered by the Google Places API, with address autocomplete and postal code validation that resolved against Pamos's shipping availability by state. The locator gave customers a clear answer — buy online or find it on a shelf nearby — without the friction of scrolling through an unsorted retailer list.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The store locator was designed to scale alongside the brand's retail expansion — new markets and retail partners could be added through the CMS without any code changes.</p>
