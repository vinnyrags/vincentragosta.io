<h2 class="wp-block-heading"><span style="text-decoration: underline;">Implementation</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A pillar-based design system with dynamic accent colors and structured data around core values.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The site was built on WordPress as a standalone microsite with its own design identity — distinct from Roundabout's main site but visually connected to the parent brand. The design system centered on four pillars, each assigned its own accent color that propagated through headings, dividers, interactive elements, and progress indicators across that pillar's pages. This wasn't a simple color swap — each pillar carried a unique visual weight that reinforced the organizational structure of the initiative itself. The data architecture underneath was equally deliberate: each pillar contained structured goal categories (Diversity, Learning, Accountability, Equitable Policies), and within each category, individual commitments tracked progress across seasons with narrative updates and measurable outcomes. The content model was built so Roundabout's communications team could publish seasonal progress reports — adding new data points, updating commitment statuses, and surfacing year-over-year comparisons — entirely through the CMS.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The complexity was in the relationships — pillars to goals, goals to commitments, commitments to seasonal progress — all managed through structured fields and rendered through templates that adapted layout and color based on context.</p>
