<h2 class="wp-block-heading"><span style="text-decoration: underline;">Background</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>New York's largest nonprofit theatre company needed a public-facing platform for institutional accountability.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>In September 2021, Roundabout Theatre Company — operator of five New York City venues including the American Airlines Theatre and Studio 54 — published a five-year strategic plan addressing equity, diversity, inclusion, and anti-racism across every level of the organization. The plan was conceived by a 29-member internal Transformation Team following a company-wide assessment that began in October 2019, and it organized commitments into four pillars: On Our Stages, In Our Offices, In Our Community, and With Our Board. Roundabout needed a dedicated digital platform — separate from the main roundabouttheatre.org site — to present these commitments publicly, report progress season by season, and demonstrate institutional accountability to audiences, donors, and the broader theatre community.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">This wasn't a marketing exercise — it was a transparency initiative backed by a $2M+ annual budget, and the site needed to reflect that seriousness in both its design and its data architecture.</p>
