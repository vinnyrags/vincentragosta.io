<h2 class="wp-block-heading"><span style="text-decoration: underline;">Results</span></h2>
<!-- /wp:heading --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading -->
<h2 class="wp-block-heading"><strong>A living transparency platform, updated across three seasons and counting.</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The site launched and has been actively maintained through three seasons of progress reporting — 2021-22, 2022-23, and 2023-24 — with Roundabout's internal team publishing updates independently each cycle. The structured content model proved its value as the initiative evolved: new commitments were added, existing ones were reclassified, and progress data accumulated without requiring structural changes to the site. The platform supported Roundabout's public accountability during a period of significant organizational action — from increasing BIPOC-owned vendor contracts to 65%, to expanding community partnerships with the New York Public Library, to launching the Theatrical Workforce Development Program with IATSE. The site earned recognition alongside Roundabout's broader EDI efforts, which were named to Crain's New York Business Diversity & Inclusion list in 2022.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"is-style-muted"} -->
<p class="is-style-muted">The design system and data architecture established for this build became a reference point for how Situation approached institutional transparency projects for other arts and nonprofit clients.</p>
